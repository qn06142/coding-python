const int MOD = 1e9 + 7;
int m, d;
string a, b;
int dp[2001][2001][2][2];

int count_magic(string &num, int pos, int mod, int is_tight, int count_d) {
    if (pos == num.size()) {
        return (mod == 0 && count_d == num.size() / 2);
    }
    
    if (dp[pos][mod][is_tight][count_d] != -1) {
        return dp[pos][mod][is_tight][count_d];
    }
    
    int limit = is_tight ? num[pos] - '0' : 9;
    int res = 0;
    
    for (int digit = 0; digit <= limit; ++digit) {
        if ((pos % 2 == 1 && digit != d) || (pos % 2 == 0 && digit == d)) {
            continue;
        }
        res = (res + count_magic(num, pos + 1, (mod * 10 + digit) % m, is_tight && (digit == limit), count_d + (pos % 2 == 1 && digit == d))) % MOD;
    }
    
    return dp[pos][mod][is_tight][count_d] = res;
}

int solve(string num) {
    memset(dp, -1, sizeof(dp));
    return count_magic(num, 0, 0, 1, 0);
}

int main() {
    cin >> m >> d;
    cin >> a >> b;
    
    int result = (solve(b) - solve(a) + MOD) % MOD;
    
    int mod = 0, count_d = 0;
    bool is_valid = true;
    for (int i = 0; i < a.size(); ++i) {
        int digit = a[i] - '0';
        if ((i % 2 == 1 && digit != d) || (i % 2 == 0 && digit == d)) {
            is_valid = false;
            break;
        }
        if (i % 2 == 1 && digit == d) {
            ++count_d;
        }
        mod = (mod * 10 + digit) % m;
    }
    
    if (is_valid && mod == 0 && count_d == a.size() / 2) {
        result = (result + 1) % MOD;
    }
    
    cout << result << endl;
    return 0;
}
