int main() {
    int N, L, R;
    cin >> N >> L >> R;
    vector<int> a(N);
    for(int i = 0; i < N; i++) {
        cin >> a[i];
    }

    vector<long long> prefix(N + 1, 0);
    for (int i = 0; i < N; i++) {
        prefix[i + 1] = prefix[i] + a[i];
    }

    long long max_sum = LLONG_MIN;
    deque<int> dq;

    for (int i = L; i <= N; i++) {

        while (!dq.empty() && dq.front() < i - R) {
            dq.pop_front();
        }

        if (i - L >= 0) {
            while (!dq.empty() && prefix[dq.back()] >= prefix[i - L]) {
                dq.pop_back();
            }
            dq.push_back(i - L);
        }

        if (!dq.empty()) {
            max_sum = max(max_sum, prefix[i] - prefix[dq.front()]);
        }
    }

    cout << max_sum << endl;
    return 0;
}