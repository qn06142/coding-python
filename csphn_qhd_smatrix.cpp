#define int long long
int cnt[(int) 1e3 + 4];
int dp[(int) 1e3 + 5][(int) 1e3 + 5];

signed main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);
    int n, m, k;
    cin >> n >> m>> k;
    for(int i = 1; i <= m; i++) {
        for(int j = 1; j <= n; j++) {
            char c;
            cin >> c;
            cnt[j] += c - '0';

        }
    }
    int a[2];
    a[0] = a[1] = 0;
    for(int j = 1;  j <= n; j++) {
        if(cnt[j] == 2) {
            continue;
        }
        a[cnt[j]]++;
    }
    dp[a[0]][a[1]] = 1;
    for(int cnt0 = n; cnt0 >= 0; cnt0--) {
        for(int cnt1 = n; cnt1 >= 0; cnt1--) {
            if(cnt0 >= 2) {
                dp[cnt0 - 2][cnt1 + 2] += 1LL * dp[cnt0][cnt1] * 1LL * (cnt0 * (cnt0 - 1) / 2 % k) % k;
                dp[cnt0 - 2][cnt1 + 2] %= k;
            }
            if(cnt0 >= 1 and cnt1 >= 1) {
                dp[cnt0 - 1][cnt1] += (long long) dp[cnt0][cnt1] * (1LL * cnt0 * cnt1) % k;
                dp[cnt0 - 1][cnt1] %= k;
            }
            if(cnt1 >= 2) {
                dp[cnt0][cnt1 - 2] += ((long long) (dp[cnt0][cnt1]) * ((long long) cnt1 * (cnt1 - 1) / 2) % k) % k;
                dp[cnt0][cnt1 - 2] %= k;
            }
        }
    }
    cout << dp[0][0];
}
