bool compare(const string &x, const string &y) {
    return x + y > y + x;
}

int main() {
    int n;
    cin >> n;
    vector<string> numbers;
    for(int i = 2; i * i <= n; i++) {
        while(n % i == 0) {
            numbers.push_back(to_string(i));
            n /= i;
        }
    }
    if(n > 1) {
        numbers.push_back(to_string(n));
    }
    sort(numbers.begin(), numbers.end(), compare);

    if (numbers[0] == "0") {
        cout << "0" << endl;
        return 0;
    }

    for (const string &num : numbers) {
        cout << num;
    }
    cout << endl;

    return 0;
}