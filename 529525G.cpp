const int MAX_SUM = 1e5 + 7;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    int n;
    cin >> n;
    vector<int> a(n + 1);
    vector<vector<bool>> dp(n + 1, vector<bool>(MAX_SUM, false));

    for(int i = 1; i <= n; i++) cin >> a[i];

    dp[0][0] = true;

    for(int i = 1; i <= n; i++) {
        for(int j = 0; j < MAX_SUM; j++) {
            dp[i][j] = dp[i - 1][j];
            if (j >= a[i]) {
                dp[i][j] = dp[i][j] || dp[i - 1][j - a[i]];
            }
        }
    }
    vector<int> ans;
    ans.reserve(n + 1);
    for(int i = 1; i < MAX_SUM; i++) {
        if (dp[n][i]) {
            ans.push_back(i);
        }
    }
    cout << ans.size() << endl;
    for(auto i:ans) cout << i << ' '; 
    return 0;
}
