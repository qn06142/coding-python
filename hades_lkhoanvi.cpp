void backtrack(int n, vector<int>& perm, vector<bool>& used) {
    if (perm.size() == n) {
        for (int i = 0; i < n; ++i) {
            cout << perm[i];
        }
        cout << '\n';
        return;
    }
    
    for (int i = 1; i <= n; ++i) {
        if (!used[i]) {
            used[i] = true;
            perm.push_back(i);
            backtrack(n, perm, used);
            perm.pop_back();
            used[i] = false;
        }
    }
}

int main() {
    int n;
    cin >> n;
    
    vector<int> perm;
    vector<bool> used(n + 1, false);  // used[0] is unused
    
    backtrack(n, perm, used);
    
    return 0;
}
