bool canPlaceCows(const vector<pair<long long, long long>>& intervals, int N, long long D) {
    int cowsPlaced = 1;
    long long lastPosition = intervals[0].first;

    for (const auto& interval : intervals) {
        while (lastPosition + D <= interval.second) {
            if (lastPosition + D <= interval.first) {
                lastPosition = interval.first;
            } else {
                lastPosition += D;
            }
            cowsPlaced++;
            if (cowsPlaced >= N) return true;
        }
    }
    return false;
}

int main() {
    int N, M;
    ifstream fin("socdist.in");
    ofstream fout("socdist.out");
    fin >> N >> M;
    vector<pair<long long, long long>> intervals(M);
    for (int i = 0; i < M; i++) {
        fin >> intervals[i].first >> intervals[i].second;
    }

    sort(intervals.begin(), intervals.end());

    long long low = 1;
    long long high = 1e18;
    long long result = 1;

    while (low <= high) {
        long long mid = low + (high - low) / 2;
        if (canPlaceCows(intervals, N, mid)) {
            result = mid;
            low = mid + 1;
        } else {
            high = mid - 1;
        }
    }

    fout << result << endl;
    fin.close();
    fout.close();
    return 0;
}
