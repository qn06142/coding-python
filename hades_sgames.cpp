int b[(int)1e5 + 5], c[(int)1e5 + 5];

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    int n;
    cin >> n;

    for (int i = 1; i <= n; i++) {
        cin >> b[i];
    }
    for (int i = 1; i <= n; i++) {
        cin >> c[i];
    }

    sort(c + 1, c + 1 + n);

    int ans = INT_MAX;

    for (int i = 1; i <= n; i++) {
        int target = -b[i];
        auto pos = lower_bound(c + 1, c + 1 + n, target);
        
        if (pos != c + 1 + n) {
            ans = min(ans, abs(*pos + b[i]));
        }
        if (pos != c + 1) {
            --pos;
            ans = min(ans, abs(*pos + b[i]));
        }
    }

    cout << ans;
    return 0;
}
