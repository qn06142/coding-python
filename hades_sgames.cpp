int main() {
    int n;
    cin >> n;

    vector<int> b(n);
    vector<int> c(n);

    for (int i = 0; i < n; ++i) {
        cin >> b[i];
    }

    for (int i = 0; i < n; ++i) {
        cin >> c[i];
    }

    sort(b.begin(), b.end());
    sort(c.begin(), c.end());

    int i = 0, j = 0;
    int minValue = INT_MAX;

    while (i < n && j < n) {
        int value = b[i] + c[j];
        minValue = min(minValue, abs(value));

        if (value < 0) {
            ++i;
        } else {
            ++j;
        }
    }

    cout << minValue << endl;

    return 0;
}