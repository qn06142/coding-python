int main() {
    #define int long long
    int n, k;
    cin >> n >> k;
    vector<int> arr(n);

    for (int i = 0; i < n; ++i) {
        cin >> arr[i];
    }

    unordered_map<int, int> freq;
    int left = 0, right = 0;
    int count = 0;

    while (right < n) {

        freq[arr[right]]++;

        while ((int) freq.size()  > k) {
            freq[arr[left]]--;
            if (freq[arr[left]] == 0) {
                freq.erase(arr[left]);
            }
            left++;
        }

        count += right - left + 1;

        right++;
    }

    cout << count << endl;

    return 0;
}