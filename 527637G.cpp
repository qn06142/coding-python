vector<bool> sieve(long long max_num) {
    vector<bool> is_prime(max_num + 1, true);
    is_prime[0] = is_prime[1] = false;
    for (long long i = 2; i * i <= max_num; ++i) {
        if (is_prime[i]) {
            for (long long j = i * i; j <= max_num; j += i) {
                is_prime[j] = false;
            }
        }
    }
    return is_prime;
}


int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);
    int N;
    cin >> N;

    long long max_A = 0;
    vector<long long> A(N);
    for (int i = 0; i < N; i++) {
        cin >> A[i];
        max_A = max(max_A, A[i]);
    }

    long long max_limit = sqrt(max_A) + 1;
    vector<bool> is_prime = sieve(max_limit);

    for (int i = 0; i < N; i++) {
        long long x = A[i];
        long long root = sqrt(x);
        if (root * root == x && root <= max_limit && is_prime[root]) {
            cout << "YES" << endl;
        } else {
            cout << "NO" << endl;
        }
    }
    return 0;
}