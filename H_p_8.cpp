const int MOD = 1e9 + 7;

int N;
vector<pair<int, int>> v;
vector<long long> sum[100005];
vector<pair<int, int>> todo[20001];

void check() {
    for (int i = 0; i <= 20000; ++i) {
        if (!todo[i].empty()) {
            int sz = todo[i].size();
            sort(todo[i].begin(), todo[i].end());
            long long cur = 0;
            for (int j = 0; j < sz; ++j) 
                cur = (cur + todo[i][j].first - todo[i][0].first) % MOD;
            for (int j = 0; j < sz; ++j) {
                if (j) 
                    cur = (cur + (2 * j - sz) * (todo[i][j].first - todo[i][j - 1].first) % MOD + MOD) % MOD;
                sum[todo[i][j].second].push_back(cur);
            }
        }
    }
}

int main() {
    freopen("triangles.in", "r", stdin);
    freopen("triangles.out", "w", stdout);
    
    cin >> N;
    v.resize(N);
    for (int i = 0; i < N; ++i) 
        cin >> v[i].first >> v[i].second;
    
    fill(begin(todo), end(todo), vector<pair<int, int>>());
    for (int i = 0; i < N; ++i) 
        todo[v[i].first + 10000].push_back({v[i].second, i});
    check();
    
    fill(begin(todo), end(todo), vector<pair<int, int>>());
    for (int i = 0; i < N; ++i) 
        todo[v[i].second + 10000].push_back({v[i].first, i});
    check();
    
    long long ans = 0;
    for (int i = 0; i < N; ++i) {
        if (sum[i].size() > 1) {
            ans = (ans + sum[i][0] * sum[i][1] % MOD) % MOD;
        }
    }
    cout << ans << "\n";
}
