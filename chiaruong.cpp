int n;
vector<int> a;
unordered_map<string, int> memo;

int countWays(int i, int sum1, int sum2, int sum3) {
    if (i == n) {
        return (sum1 == sum2 && sum2 == sum3) ? 1 : 0;
    }

    string key = to_string(i) + "-" + to_string(sum1) + "-" + to_string(sum2) + "-" + to_string(sum3);
    if (memo.find(key) != memo.end()) {
        return memo[key];
    }

    int count = 0;
    count += countWays(i + 1, sum1 + a[i], sum2, sum3);
    count += countWays(i + 1, sum1, sum2 + a[i], sum3);
    count += countWays(i + 1, sum1, sum2, sum3 + a[i]);

    memo[key] = count;
    return count;
}

int main() {
    cin >> n;
    a.resize(n);
    for (int i = 0; i < n; ++i) {
        cin >> a[i];
    }

    int total_sum = accumulate(a.begin(), a.end(), 0);
    if (total_sum % 3 != 0) {
        cout << -1 << endl;
        return 0;
    }

    int result = countWays(0, 0, 0, 0);
    cout << (result > 0 ? result : -1) << endl; 

    return 0;
}