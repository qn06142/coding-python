vector<vector<int>> adj;
vector<int> produce;
vector<bool> visited;

int bfs(int start) {
    queue<int> q;
    q.push(start);
    visited[start] = true;
    int total = 0;

    while (!q.empty()) {
        int u = q.front();
        q.pop();
        total += produce[u];

        for (int v : adj[u]) {
            if (!visited[v]) {
                visited[v] = true;
                q.push(v);
            }
        }
    }
    return total;
}

int main() {
    int n, m, k;
    cin >> n >> m >> k;
    
    produce.resize(n);
    adj.resize(n);
    visited.resize(n, false);
    
    for (int i = 0; i < n; ++i) {
        cin >> produce[i];
    }
    
    for (int i = 0; i < m; ++i) {
        int u, v;
        cin >> u >> v;
        adj[u - 1].push_back(v - 1);
    }
    
    vector<int> component_values;

    for (int i = 0; i < n; ++i) {
        if (!visited[i]) {
            component_values.push_back(bfs(i));
        }
    }
    
    sort(component_values.rbegin(), component_values.rend());
    
    int max_sum = 0;
    for (int i = 0; i < k && i < component_values.size(); ++i) {
        max_sum += component_values[i];
    }
    
    cout << max_sum << endl;
    
    return 0;
}
