int main() {
    int n;
    cin >> n;

    vector<int> a(n), b(n);

    for (int i = 0; i < n; ++i) {
        cin >> a[i];
    }

    for (int i = 0; i < n; ++i) {
        cin >> b[i];
    }

    vector<pair<double, pair<int, int>>> cars;

    for (int i = 0; i < n; ++i) {
        cars.push_back({(double)a[i] / b[i], {a[i], b[i]}});
    }

    sort(cars.rbegin(), cars.rend());

    long long penalty = 0, curr = 0;

    for (auto car : cars) {
        int ai = car.second.first;
        int bi = car.second.second;
        curr += bi;
        penalty += curr * ai;
    }
    cout << penalty;
    return 0;
}
