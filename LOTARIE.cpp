int minChanges(string s, int n) {
    if (n == 0) return 0;

    vector<vector<int>> dp(n, vector<int>(3, 0));

    auto charToIndex = [](char c) {
        return c == 'A' ? 0 : (c == 'B' ? 1 : 2);
    };

    for (int i = 0; i < 3; ++i) {
        dp[0][i] = (charToIndex(s[0]) == i) ? 0 : 1;
    }

    for (int i = 1; i < n; ++i) {
        for (int j = 0; j < 3; ++j) {
            dp[i][j] = INT_MAX;
            for (int k = 0; k < 3; ++k) {
                if (j != k) {
                    dp[i][j] = min(dp[i][j], dp[i-1][k] + (charToIndex(s[i]) == j ? 0 : 1));
                }
            }
        }
    }

    return min({dp[n-1][0], dp[n-1][1], dp[n-1][2]});
}

signed main() {
    int N;
    string s;
    cin >> N >> s;

    cout << minChanges(s, N) << endl;

    return 0;
}