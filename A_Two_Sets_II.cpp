const int MOD = 1000000007;

using namespace std;

int main() {
    int n;
    cin >> n;

    int sum = n * (n + 1) / 2;

    if (sum % 2 != 0) {
        cout << 0 << endl;
        return 0;
    }

    int target = sum / 2;

    vector<int> dp(target + 1, 0);
    dp[0] = 1; 

    for (int i = 1; i <= n; ++i) {
        for (int j = target; j >= i; --j) {
            dp[j] = (dp[j] + dp[j - i]) % MOD;
        }
    }

    int result = (dp[target] * 1LL * (MOD + 1) / 2) % MOD;
    cout << result << endl;

    return 0;
}