#include <bits/stdc++.h>
using namespace std;

int a[15][15];
int b[15][15];

bool is_valid(int n, int m) {
    for (int i = 0; i < n; i++) {
        for (int j = 0; j < m; j++) {
            if (a[i][j] == b[i][j]) {
                return false;
            }
        }
    }
    return true;
}

void solve() {
    int q;
    cin >> q;
    while (q--) {
        int n, m;
        cin >> n >> m;
        vector<int> elements;
        for (int i = 0; i < n; i++) {
            for (int j = 0; j < m; j++) {
                cin >> a[i][j];
                elements.push_back(a[i][j]);
            }
        }

        if (n == 1 && m == 1) {
            cout << -1 << endl;
            continue;
        }

        sort(elements.begin(), elements.end());

        bool found = false;
        do {
            int idx = 0;
            for (int i = 0; i < n; i++) {
                for (int j = 0; j < m; j++) {
                    b[i][j] = elements[idx++];
                }
            }
            if (is_valid(n, m)) {
                found = true;
                for (int i = 0; i < n; i++) {
                    for (int j = 0; j < m; j++) {
                        cout << b[i][j] << " ";
                    }
                    cout << endl;
                }
                break;
            }
        } while (next_permutation(elements.begin(), elements.end()));

        if (!found) {
            cout << -1 << endl;
        }
    }
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(0);
    solve();
    return 0;
}
