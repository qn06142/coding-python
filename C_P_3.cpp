int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    int n;
    cin >> n;
    
    int first, previous, current;
    cin >> first;
    previous = first;
    
    bool missingNumbers = false;

    for (int j = 1; j < first; ++j) {
        cout << j << '\n';
        missingNumbers = true;
    }

    for (int i = 1; i < n; ++i) {
        cin >> current;
        for (int j = previous + 1; j < current; ++j) {
            cout << j << '\n';
            missingNumbers = true;
        }
        previous = current;
    }

    if (!missingNumbers) {
        cout << "good job" << '\n';
    }

    return 0;
}
