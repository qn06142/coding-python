int main() {
    ios_base::sync_with_stdio(false);
    int N;
    cin >> N;

    vector<long long> a(N+1); 
    for (int i = 1; i <= N; ++i) {
        cin >> a[i];
    }

    if (N == 1) {
        cout << a[1] << endl;
        return 0;
    }
    if (N == 2) {
        cout << a[1] + a[2] << endl;
        return 0;
    }

    vector<long long> dp(N+1, 0); 

    dp[1] = a[1]; 
    dp[2] = a[1] + a[2]; 

    for (int i = 3; i <= N; ++i) {
        dp[i] = max({dp[i-1], a[i] + dp[i-2], a[i] + a[i-1] + dp[i-3]});
    }

    cout << dp[N] << endl; 

    return 0;
}