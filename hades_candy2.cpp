void gen(int n, int m, int start, int& count, int sum, int curr_count) {
    if (curr_count == n) {
        if (sum == m) {
            count++;
        }
        return;
    }

    for (int i = start; i <= m - sum; i++) {
        gen(n, m, i, count, sum + i, curr_count + 1);
    }
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    int n, m;
    cin >> n >> m;

    int count = 0;
    gen(n, m, 1, count, 0, 0);

    cout << count << endl;

    return 0;
}

