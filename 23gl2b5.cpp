#pragma GCC optimize("fast-math")
#define int long long
using namespace std;
int n;
int a[(int) 1e5 + 5];

void bacctracc(int l, int r, int sum, vector<int> &c, bool chosen = false) {
    if (l > r) {
        if(chosen)
        c.push_back(sum);
        return;
    }
    bacctracc(l + 1, r, sum + a[l], c, 1);
    bacctracc(l + 1, r, sum, c, chosen);
}
void mitm() {
    for (int i = 1; i <= n; i++) {
        cin >> a[i];
    }

    vector<int> cnt1, cnt2;
    int mid = n / 2;

    bacctracc(1, mid, 0, cnt1);
    bacctracc(mid + 1, n, 0, cnt2);

    sort(cnt1.begin(), cnt1.end());
    sort(cnt2.begin(), cnt2.end());
    int sum = accumulate(a + 1, a + n + 1, 0LL);
    int ans = LLONG_MAX;
    for (int l : cnt1) {
        auto it = lower_bound(cnt2.begin(), cnt2.end(), sum / 2 - l);
        if (it != cnt2.end()) {
            ans = min(ans, abs(sum - 2 * (l + *it)));
        }
        if (it != cnt2.begin()) {
            --it;
            ans = min(ans, abs(sum - 2 * (l + *it)));
        }
    }

    cout << ans << endl;
}
bitset<(int) (12 * 1e5 + 5)> dp;
void dp_() {
    for (int i = 1; i <= n; i++) {
        cin >> a[i];
    }
    int sum = 0;
    for (int i = 1; i <= n; i++)
        sum += a[i];
    
    dp[0] = 1;
    for(int i = 1; i <= n; i++) {
        //copy
        auto dpsplit = dp << a[i];
        dp |= dpsplit;
    }
    int diff = INT_MAX;
    for(int i = 0; i <= sum; i++) {
        if(dp[i]) {
            diff = min(diff, abs(sum - 2 * i));
        }
    }
    cout << diff;
}
