int main() {
    int n;
    long long k;
    cin >> n >> k;

    vector<int> X(n);
    for (int i = 0; i < n; ++i) {
        cin >> X[i];
    }

    for (int i = 0; i < n && k > 0; ++i) {

        priority_queue<pair<int, int>, vector<pair<int, int>>, greater<pair<int, int>>> pq;

        for (int j = i; j < n && j <= i + k; ++j) {
            pq.push({X[j], j});
        }

        auto [minVal, minPos] = pq.top();

        int swaps = minPos - i;

        if (swaps <= k) {
            k -= swaps;
            for (int j = minPos; j > i; --j) {
                swap(X[j], X[j - 1]);
            }
        }
    }

    for (int i = 0; i < n; ++i) {
        if (i > 0) cout << " ";
        cout << X[i];
    }
    cout << endl;

    return 0;
}