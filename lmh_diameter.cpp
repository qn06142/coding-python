#pragma GCC optimize("Ofast")
static const int MAXN = 200000 + 5;
static const int LOG  = __lg(MAXN) + 1;

int n;
vector<pair<int,int>> adj[MAXN];  
int par[MAXN];
int level[MAXN];           
long long dist_root[MAXN]; 
int jmp[MAXN][LOG];

int tin[MAXN], tout[MAXN], rev_tin[MAXN], timer = 0;

void dfs_lca(int u, int p) {
    par[u] = p;
    tin[u] = ++timer;
    rev_tin[timer] = u;
    for (auto &pr : adj[u]) {
        int v = pr.first, w = pr.second;
        if (v == p) continue;
        level[v] = level[u] + 1;
        dist_root[v] = dist_root[u] + w;
        dfs_lca(v, u);
    }
    tout[u] = timer;
}

void build_lca() {
    for (int u = 1; u <= n; u++) {
        jmp[u][0] = par[u];
    }
    for (int b = 1; b < LOG; b++) {
        for (int u = 1; u <= n; u++) {
            int m = jmp[u][b-1];
            jmp[u][b] = (m > 0 ? jmp[m][b-1] : 0);
        }
    }
}

int jump_up(int u, int k) {
    for (int b = 0; b < LOG; b++) {
        if (k & (1 << b)) {
            u = jmp[u][b];
            if (u == 0) break;
        }
    }
    return u;
}

int lca(int u, int v) {
    if (level[u] < level[v]) swap(u, v);
    u = jump_up(u, level[u] - level[v]);
    if (u == v) return u;
    for (int b = LOG - 1; b >= 0; b--) {
        if (jmp[u][b] != jmp[v][b]) {
            u = jmp[u][b];
            v = jmp[v][b];
        }
    }
    return par[u];
}

long long dist_uv(int u, int v) {
    int w = lca(u, v);
    return dist_root[u] + dist_root[v] - 2 * dist_root[w];
}

struct Node {
    int a, b;
    long long diam;
};
vector<Node> seg;

Node merge_node(const Node &X, const Node &Y) {
    if (X.diam < 0) return Y;
    if (Y.diam < 0) return X;
    Node R = (X.diam > Y.diam ? X : Y);
    for (int xa : {X.a, X.b}) {
        for (int yb : {Y.a, Y.b}) {
            long long d = dist_uv(xa, yb);
            if (d > R.diam) {
                R.diam = d;
                R.a = xa;
                R.b = yb;
            }
        }
    }
    return R;
}

void build_seg(int idx, int l, int r) {
    if (l == r) {
        int u = rev_tin[l];
        seg[idx] = {u, u, 0LL};
        return;
    }
    int m = (l + r) >> 1;
    build_seg(idx<<1,   l,   m);
    build_seg(idx<<1|1, m+1, r);
    seg[idx] = merge_node(seg[idx<<1], seg[idx<<1|1]);
}

Node query_seg(int idx, int l, int r, int ql, int qr) {
    if (qr < l || r < ql) return {0, 0, -1LL};
    if (ql <= l && r <= qr) return seg[idx];
    int m = (l + r) >> 1;
    Node L = query_seg(idx<<1,   l,   m, ql, qr);
    Node R = query_seg(idx<<1|1, m+1, r,   ql, qr);
    return merge_node(L, R);
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    cin >> n;
    for (int i = 1; i < n; i++) {
        int u, v, c;
        cin >> u >> v >> c;
        adj[u].push_back({v,c});
        adj[v].push_back({u,c});
    }

    level[1] = 0;
    dist_root[1] = 0;
    dfs_lca(1, 0);

    build_lca();

    seg.assign(4*(n+1), {0, 0, -1LL});
    build_seg(1, 1, n);

    long long answer = LLONG_MAX;
    for (int u = 1; u <= n; u++) {
        for (auto &pr : adj[u]) {
            int v = pr.first;
            if (par[v] != u) continue; 

            Node A = query_seg(1, 1, n, tin[v], tout[v]);

            Node B1 = (tin[v] > 1 ? query_seg(1, 1, n, 1, tin[v]-1) : Node{0,0,-1LL});
            Node B2 = (tout[v] < n ? query_seg(1, 1, n, tout[v]+1, n) : Node{0,0,-1LL});
            Node B  = merge_node(B1, B2);
            answer = min(answer, max(A.diam, B.diam));
        }
    }

    cout << answer;
}