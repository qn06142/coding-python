#define maxn 1000000 + 5
struct Node {
    int pairs = 0;
    int open = 0;
    int closed = 0;
    Node() : pairs(0), open(0), closed(0) {}
};

Node nullnode;
Node seq[4 * maxn];
string a;
int n;

Node merge(Node a, Node b) {
    Node ans;
    int matched = min(a.open, b.closed);
    ans.pairs = a.pairs + b.pairs + matched;
    ans.open = a.open + b.open - matched;
    ans.closed = a.closed + b.closed - matched;
    return ans;
}

void build(int node = 1, int l = 1, int r = n) {
    if (l == r) {
        seq[node].pairs = 0;
        seq[node].open = (a[l - 1] == '(') ? 1 : 0; // Adjust for 0-based index
        seq[node].closed = !seq[node].open;
    } else {
        int mid = (l + r) / 2;
        build(node * 2, l, mid);
        build(node * 2 + 1, mid + 1, r);
        seq[node] = merge(seq[node * 2], seq[node * 2 + 1]);
    }
}

Node getsum(int x, int y, int node = 1, int l = 1, int r = n) {
    if (x > r || y < l) {
        return nullnode;
    }
    if (x <= l && y >= r) {
        return seq[node];
    }
    int mid = (l + r) / 2;
    Node L = getsum(x, y, node * 2, l, mid);
    Node R = getsum(x, y, node * 2 + 1, mid + 1, r);
    return merge(L, R);
}

int main() {
    cin >> a;
    n = a.length();
    nullnode = Node();
    int m;
    cin >> m;
    build();

    while (m--) {
        int x, y;
        cin >> x >> y;
        cout << 2 * getsum(x, y).pairs << '\n';
    }

    return 0;
}
