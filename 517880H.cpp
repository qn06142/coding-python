#define int long long
const int INF = INT_MAX;
int N, M, K;
vector<vector<int>> adj;
vector<vector<int>> dist;
vector<int> special;

void bfs(int start, int idx) {
    queue<int> q;
    vector<int> d(N + 1, INF);
    d[start] = 0;
    q.push(start);
    
    while (!q.empty()) {
        int node = q.front();
        q.pop();
        
        for (int neighbor : adj[node]) {
            if (d[neighbor] == INF) {
                d[neighbor] = d[node] + 1;
                q.push(neighbor);
            }
        }
    }
    
    for (int i = 0; i < K; ++i) {
        dist[idx][i] = d[special[i]];
    }
}

signed main() {
    ifstream fin("OPERATION.INP");
    ofstream fout("OPERATION.OUT");
    fin >> N >> M >> K;
    adj.resize(N + 1);
    special.resize(K);
    dist.resize(K, vector<int>(K));
    
    for (int i = 0; i < M; ++i) {
        int u, v;
        fin >> u >> v;
        adj[u].push_back(v);
        adj[v].push_back(u);
    }
    
    for (int i = 0; i < K; ++i) {
        fin >> special[i];
    }
    
    for (int i = 0; i < K; ++i) {
        bfs(special[i], i);
    }
    
    vector<int> dp(1 << K, INF);
    dp[(1 << K) - 1] = 0;
    
    for (int mask = (1 << K) - 1; mask > 0; --mask) {
        for (int i = 0; i < K; ++i) {
            if ((mask >> i) & 1) {
                for (int j = i + 1; j < K; ++j) {
                    if ((mask >> j) & 1) {
                        int new_mask = mask ^ (1 << i) ^ (1 << j);
                        dp[new_mask] = min(dp[new_mask], dp[mask] + dist[i][j]);
                    }
                }
            }
        }
    }
    
    fout << ((dp[0] == INT_MAX) ? -1 : dp[0]) << '\n';
    fin.close();
    fout.close();
    return 0;
}
