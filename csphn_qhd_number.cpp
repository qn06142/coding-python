const int maxn = 3001;
int m, n;
string x, y;
pair<int, char> dp[maxn][maxn];
void init() {
    for(int i = 0; i <= m; i++) {
        dp[i][n] = {0, 'a'};
    }
    for(int j = 0; j <= n; j++) {
        dp[m][j] = {0, 'a'};
    }
}
int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);
    cin >> x >> y;
    m = x.size();
    n = y.size();
    for(int i = m - 1; i >= 0; i--) {
        for(int j = n - 1; j >= 0; j--) {
            if(x[i] == y[j]) {
                dp[i][j] = {dp[i + 1][j + 1].first, x[i]};
            } else {
                dp[i][j] = max(dp[i + 1][j], dp[i][j + 1]);
            }
        }
    }
    pair<int, char> maxpair = {0, 'a'};
    for(int i = 0; i < m; i++) {
        for(int j = 0; j < n; j++) {
            if(dp[i][j].second != '0' and dp[i][j] > maxpair) {
                maxpair = dp[i][j];
            }
        }
    }
    if(maxpair.first == 01) {
        cout << 0;
        return 0;
    }
    int len = maxpair.first;
    char c = maxpair.second;
    int i = 0, j = 0;
    while(len-- >= 0) {
        cout << c;
        while(x[i] != c) i++;
        while(y[j] != c) j++;
        i ++;
        j ++;
        c = dp[i][j].second;
    }
}
