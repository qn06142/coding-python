const int MAXN = 1e4 + 1;
const int OFFSET = 5000;
const int MAXCOUNT = 3e5;

int n;
int a[MAXN];
int pref[MAXN];
int count_[MAXCOUNT];

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    cin >> n;
    for (int i = 1; i <= n; i++) {
        cin >> a[i];
    }

    for (int mid = 1; mid <= n; mid++) {
        int ans = 0;
        fill(count_, count_ + MAXCOUNT, 0);
        count_[OFFSET]++;

        for (int i = 1; i <= n; i++) {
            pref[i] = pref[i - 1] + ((a[i] <= a[mid]) ? 1 : -1);
            if(i < mid) count_[pref[i] + OFFSET]++;
            else ans += count_[pref[i] + OFFSET] + count_[pref[i] - 1 + OFFSET];
        }
        cout << ans << ' ';
    }
}