int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);
    
    map<int, int> servings;  // key: deliciousness, value: servings
    set<pair<int, int>> popular;  // stores (servings, deliciousness) pairs
    
    int q;
    cin >> q;
    
    while (q--) {
        int t;
        cin >> t;
        if (t == 1) {
            int x, k;
            cin >> x >> k;
            if (servings[x] > 0) {
                popular.erase({servings[x], x});
            }
            servings[x] += k;
            popular.insert({servings[x], x});
        } else if (t == 2) {
            int x, k;
            cin >> x >> k;
            if (servings[x] > 0) {
                popular.erase({servings[x], x});
            }
            servings[x] -= k;
            if (servings[x] > 0) {
                popular.insert({servings[x], x});
            } else {
                servings.erase(x);
            }
        } else if (t == 3) {
            int x;
            cin >> x;
            auto it = servings.upper_bound(x);
            if (it == servings.begin()) {
                cout << -1 << '\n';
            } else {
                --it;
                cout << it->first << '\n';
            }
        } else if (t == 4) {
            if (popular.empty()) {
                cout << -1 << '\n';
            } else {
                auto it = popular.rbegin();  // reverse iterator to get the largest (servings, deliciousness)
                cout << it->second << " " << it->first << '\n';
            }
        }
    }
    return 0;
}
