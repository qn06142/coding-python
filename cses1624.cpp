const int N = 8;
vector<int> cols(N, -1); 
vector<vector<char>> board(N, vector<char>(N));

bool isSafe(int row, int col) {
    for (int i = 0; i < row; ++i) {
        if (cols[i] == col || abs(cols[i] - col) == abs(i - row)) {
            return false;
        }
    }
    return true;
}

int placeQueens(int row) {
    if (row == N) {
        return 1; 
    }

    int count = 0;
    for (int col = 0; col < N; ++col) {
        if (board[row][col] == '.' && isSafe(row, col)) {
            cols[row] = col;
            count += placeQueens(row + 1);
            cols[row] = -1; 
        }
    }
    return count;
}

int main() {

    for (int i = 0; i < N; ++i) {
        for (int j = 0; j < N; ++j) {
            cin >> board[i][j];
        }
    }

    cout << placeQueens(0) << endl;

    return 0;
}