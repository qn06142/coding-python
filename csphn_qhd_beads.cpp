const int N = 1e5+5;
int n, a[N], s[N], F0[N], F1[N], k, res;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    cin >> n;
    for (int i = 1; i <= n; i++) cin >> a[i];

    k = 1;
    s[1] = a[n];
    F0[n] = 1;
    for (int i = n-1; i >= 1; i--) {
        int j = k;
        while (j > 0 && a[i] >= s[j]) j--;
        if (j == k) s[++k] = 0;
        s[j+1] = max(s[j+1], a[i]);
        F0[i] = j+1;
    }

    k = 1;
    memset(s, 0, sizeof(s));
    s[1] = a[n];
    F1[n] = 1;
    for (int i = n-1; i >= 1; i--) {
        int j = k;
        while (j > 0 && a[i] <= s[j]) j--;
        if (j == k) s[++k] = 1e9+7;
        s[j+1] = min(s[j+1], a[i]);
        F1[i] = j+1;
    }

    for (int i = 1; i <= n; i++) res = max(res, F0[i] + F1[i] - 1);
    cout << res << "\n";

    return 0;
}
