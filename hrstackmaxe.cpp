    for (int i = 0; i < N; ++i) {
        int type;
        cin >> type;

        if (type == 1) {
            int x;
            cin >> x;
            stacc.push(x);

            if (maxstacc.empty() || x >= maxstacc.top()) {
                maxstacc.push(x);
            } else {
                maxstacc.push(maxstacc.top());
            }
        } else if (type == 2) {
            stacc.pop();
            maxstacc.pop();
        } else if (type == 3) {
            cout << maxstacc.top() << '\n';
        }
    }

    return 0;
