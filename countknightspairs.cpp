#define fast_io ios_base::sync_with_stdio(false); cin.tie(nullptr);

typedef long long ll;

ll count_pairs(const vector<int>& counts) {
    ll total_pairs = 0;
    for (const int count : counts) {
        if (count > 1) {
            total_pairs += static_cast<ll>(count) * (count - 1) / 2;
        }
    }
    return total_pairs;
}

int main() {
    fast_io;

    long long n, m, k;
    cin >> n >> m >> k;

    vector<int> row_count(n + 1, 0), col_count(m + 1, 0);
    vector<int> main_diag_count(n + m + 1, 0), anti_diag_count(n + m + 1, 0);

    int offset = m;  

    vector<pair<int, int>> queens(k);
    for (int i = 0; i < k; ++i) {
        int x, y;
        cin >> x >> y;
        queens[i] = {x, y};
        row_count[x]++;
        col_count[y]++;
        main_diag_count[x - y + offset]++;
        anti_diag_count[x + y]++;
    }

    ll total_pairs = 0;
    total_pairs += count_pairs(row_count);
    total_pairs += count_pairs(col_count);
    total_pairs += count_pairs(main_diag_count);
    total_pairs += count_pairs(anti_diag_count);

    cout << (k * (k - 1) / 2) - total_pairs << endl;

    return 0;
}