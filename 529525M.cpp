int main() {
    int N, K;
    cin >> N >> K;

    vector<int> h(N + 1);
    for (int i = 1; i <= N; ++i) {
        cin >> h[i];
    }

    vector<long long> dp(N + 1, LLONG_MAX); 
    dp[1] = 0; 

    deque<int> dq;
    dq.push_back(1);

    for (int i = 2; i <= N; ++i) {

        while (!dq.empty() && dq.front() < i - K) {
            dq.pop_front();
        }

        dp[i] = dp[dq.front()] + abs(h[i] - h[dq.front()]);

        while (!dq.empty() && dp[dq.back()] >= dp[i]) {
            dq.pop_back();
        }

        dq.push_back(i);
    }

    cout << dp[N] << endl; 

    return 0;
}