int min_energy(int n, int k, vector<vector<int>>& matrix) {

    map<int, vector<pair<int, int>>> positions;
    for (int i = 0; i < n; ++i) {
        for (int j = 0; j < n; ++j) {
            int val = matrix[i][j];
            if (val <= k) {
                positions[val].emplace_back(i, j);
            }
        }
    }

    for (int value = 1; value <= k; ++value) {
        if (positions.find(value) == positions.end()) {
            return -1;
        }
    }

    vector<vector<int>> dp(n, vector<int>(n, INT_MAX));
    for (auto& p : positions[1]) {
        dp[p.first][p.second] = 0;
    }

    for (int value = 2; value <= k; ++value) {
        vector<vector<int>> new_dp(n, vector<int>(n, INT_MAX));
        vector<int> min_row(n, INT_MAX);
        vector<int> min_col(n, INT_MAX);

        for (auto& p : positions[value - 1]) {
            int i1 = p.first, j1 = p.second;
            int dp_value = dp[i1][j1];
            for (int i = 0; i < n; ++i) {
                min_row[i] = min(min_row[i], dp_value + (i1 - i) * (i1 - i));
                min_col[i] = min(min_col[i], dp_value + (j1 - i) * (j1 - i));
            }
        }

        for (auto& p : positions[value]) {
            int i2 = p.first, j2 = p.second;
            new_dp[i2][j2] = min(new_dp[i2][j2], min_row[i2]);
            new_dp[i2][j2] = min(new_dp[i2][j2], min_col[j2]);
        }

        dp = new_dp;
    }

    int min_energy = INT_MAX;
    for (auto& p : positions[k]) {
        min_energy = min(min_energy, dp[p.first][p.second]);
    }

    return (min_energy == INT_MAX) ? -1 : min_energy;
}

int main() {
    int n, k;
    cin >> n >> k;

    vector<vector<int>> matrix(n, vector<int>(n));
    for (int i = 0; i < n; ++i) {
        for (int j = 0; j < n; ++j) {
            cin >> matrix[i][j];
        }
    }

    int result = min_energy(n, k, matrix);
    cout << result << endl;

    return 0;
}