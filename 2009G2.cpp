int getminimumoperations(int k, multiset<int>& freqset) {
    return k - (*freqset.rbegin());  // Largest frequency gives minimum operations
}

class segmenttree {
public:
    segmenttree(int size) {
        n = 1;
        while (n < size) n *= 2;
        tree.assign(2 * n, 0);
        lazy.assign(2 * n, -1);
        rangebegin.resize(2 * n);
        rangeend.resize(2 * n);
        
        // initialize leaf nodes
        for (int i = n; i < 2 * n; ++i) {
            rangebegin[i] = rangeend[i] = i - n;
        }
        // initialize internal nodes
        for (int i = n - 1; i > 0; --i) {
            rangebegin[i] = rangebegin[2 * i];
            rangeend[i] = rangeend[2 * i + 1];
        }
    }

    void rangeset(int lo, int hi, int value) {
        rangeset(1, lo, hi, value);
    }

    int query(int lo, int hi) {
        return query(1, lo, hi);
    }

private:
    int n;
    vector<int> tree, lazy, rangebegin, rangeend;

    void propagate(int node) {
        if (lazy[node] != -1) {
            lazy[2 * node] = lazy[2 * node + 1] = lazy[node];
            tree[2 * node] = lazy[node] * (rangeend[2 * node] - rangebegin[2 * node] + 1);
            tree[2 * node + 1] = lazy[node] * (rangeend[2 * node + 1] - rangebegin[2 * node + 1] + 1);
            lazy[node] = -1;
        }
    }

    void rangeset(int node, int lo, int hi, int value) {
        if (rangebegin[node] == lo && rangeend[node] == hi) {
            tree[node] = value * (hi - lo + 1);
            lazy[node] = value;
            return;
        }
        propagate(node);
        if (rangebegin[2 * node] <= hi && rangeend[2 * node] >= lo) {
            rangeset(2 * node, lo, min(hi, rangeend[2 * node]), value);
        }
        if (rangebegin[2 * node + 1] <= hi && rangeend[2 * node + 1] >= lo) {
            rangeset(2 * node + 1, max(lo, rangebegin[2 * node + 1]), hi, value);
        }
        tree[node] = tree[2 * node] + tree[2 * node + 1];
    }

    int query(int node, int lo, int hi) {
        if (rangebegin[node] == lo && rangeend[node] == hi) return tree[node];
        propagate(node);
        int result = 0;
        if (rangebegin[2 * node] <= hi && rangeend[2 * node] >= lo) {
            result += query(2 * node, lo, min(hi, rangeend[2 * node]));
        }
        if (rangebegin[2 * node + 1] <= hi && rangeend[2 * node + 1] >= lo) {
            result += query(2 * node + 1, max(lo, rangebegin[2 * node + 1]), hi);
        }
        return result;
    }
};

signed main() {
    int t;
    cin >> t;
    while (t--) {
        int n, k, q;
        cin >> n >> k >> q;
        vector<int> v(n);
        
        // preprocess the array to normalize it
        for (int i = 0; i < n; ++i) {
            cin >> v[i];
            v[i] -= i;
        }
        
        vector<int> ans(n);  // Holds the minimum operations for subarrays
        map<int, int> freqmap;
        multiset<int> activefrequencies;
        
        // initialize the first window of size k
        for (int i = 0; i < k; ++i) {
            if (freqmap[v[i]] == 0) {
                freqmap[v[i]] = 1;
                activefrequencies.insert(1);
            } else {
                activefrequencies.erase(activefrequencies.find(freqmap[v[i]]));
                freqmap[v[i]]++;
                activefrequencies.insert(freqmap[v[i]]);
            }
        }
        ans[0] = getminimumoperations(k, activefrequencies);
        
        // slide the window over the array
        for (int i = k; i < n; ++i) {
            activefrequencies.erase(activefrequencies.find(freqmap[v[i - k]]));
            if (--freqmap[v[i - k]] > 0) {
                activefrequencies.insert(freqmap[v[i - k]]);
            }

            if (freqmap[v[i]] == 0) {
                freqmap[v[i]] = 1;
                activefrequencies.insert(1);
            } else {
                activefrequencies.erase(activefrequencies.find(freqmap[v[i]]));
                freqmap[v[i]]++;
                activefrequencies.insert(freqmap[v[i]]);
            }
            ans[i - k + 1] = getminimumoperations(k, activefrequencies);
        }
        
        vector<array<int, 3>> queries(q);
        vector<int> results(q);
        
        // Read and adjust queries
        for (int i = 0; i < q; ++i) {
            cin >> queries[i][0] >> queries[i][1];
            queries[i][0]--;
            queries[i][1]--;
            queries[i][2] = i;
            queries[i][0] *= -1;  // invert to sort in reverse order
        }
        sort(queries.begin(), queries.end());
        for (int i = 0; i < q; ++i) queries[i][0] *= -1;  // Revert inversion
        
        segmenttree st(n);
        st.rangeset(n - k, n - k, ans[n - k]);
        int cur = n - k;
        stack<pair<int, int>> s;
        s.push({ans[n - k], n - k});
        
        // process queries
        for (int i = 0; i < q; ++i) {
            while (cur != queries[i][0]) {
                cur--;
                int updateindex = cur;
                while (!s.empty() && s.top().first > ans[cur]) {
                    updateindex = s.top().second;
                    s.pop();
                }
                s.push({ans[cur], updateindex});
                st.rangeset(cur, updateindex, ans[cur]);
            }
            results[queries[i][2]] = st.query(queries[i][0], queries[i][1] - k + 1);
        }
        
        // output results
        for (const auto& result : results) {
            cout << result << endl;
        }
    }
    return 0;
}
