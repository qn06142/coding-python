vector<int> digits;
long long dp[20][2][2];

long long countPalindromes(int pos, bool tight, bool leadingZeros, int len) {
    if (pos >= len / 2) return 1; // reached halfway
    
    if (dp[pos][tight][leadingZeros] != -1) return dp[pos][tight][leadingZeros];
    
    int limit = tight ? digits[pos] : 9;
    long long ans = 0;
    
    for (int d = 0; d <= limit; ++d) {
        if (leadingZeros && d == 0) {
            ans += countPalindromes(pos + 1, tight && (d == limit), true, len);
        } else {
            int mirrorPos = len - pos - 1;
            if (d == digits[mirrorPos] || (tight && pos == mirrorPos)) {
                ans += countPalindromes(pos + 1, tight && (d == limit), false, len);
            }
        }
    }
    
    return dp[pos][tight][leadingZeros] = ans;
}

long long solve(long long x) {
    if (x < 0) return 0;
    digits.clear();
    
    while (x > 0) {
        digits.push_back(x % 10);
        x /= 10;
    }
    
    reverse(digits.begin(), digits.end());
    int len = digits.size();
    memset(dp, -1, sizeof(dp));
    
    return countPalindromes(0, true, true, len);
}

int main() {
    long long a, b;
    cin >> a >> b;
    
    cout << solve(b) - solve(a - 1) << endl;
    
    return 0;
}
