int main() {
    #define int long long
    int n;
    cin >> n;
    vector<int> s(n);
    for (int i = 0; i < n; ++i) {
        cin >> s[i];
    }
    
    stack<int> st;
    int max_strength = 0;
    int i = 0;
    
    while (i < n) {
        if (st.empty() || s[st.top()] <= s[i]) {
            st.push(i++);
        } else {
            int tp = st.top();
            st.pop();
            int width = st.empty() ? i : i - st.top() - 1;
            max_strength = max(max_strength, s[tp] * width);
        }
    }
    
    while (!st.empty()) {
        int tp = st.top();
        st.pop();
        int width = st.empty() ? i : i - st.top() - 1;
        max_strength = max(max_strength, s[tp] * width);
    }
    
    cout << max_strength << endl;
    return 0;
}
