void comb(vector<int>& permutation, vector<bool>& used, int n, int k) {
    if (permutation.size() == k) {
        for (int i = 0; i < k; ++i) {
            cout << permutation[i] << " ";
        }
        cout << endl;
        return;
    }

    for (int i = 1; i <= n; ++i) {
        if (!used[i]) {
            used[i] = true;
            permutation.push_back(i);
            comb(permutation, used, n, k);
            permutation.pop_back();
            used[i] = false;
        }
    }
}

int main() {
    int n, k;
    cin >> n >> k;

    vector<int> permutation;
    vector<bool> used(n + 1, false);

    comb(permutation, used, n, k);

    return 0;
}
