const int MAX_N = 100005;
int a[MAX_N], pref[MAX_N];
int n, k;

bool check(int x) {
    int cnt = 1;
    int currentSum = 0;
    for (int i = 1; i <= n; i++) {
        if (currentSum + a[i] > x) {
            cnt++;
            currentSum = a[i];
            if (cnt > k) return false;
        } else {
            currentSum += a[i];
        }
    }
    return true;
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    cin >> n >> k;
    vector<int> weights(n + 1);
    for (int i = 1; i <= n; i++) {
        cin >> weights[i];
        a[i] = weights[i];
    }

    int left = *max_element(weights.begin() + 1, weights.end());
    int right = accumulate(weights.begin() + 1, weights.end(), 0);
    int answer = right;

    while (left <= right) {
        int mid = left + (right - left) / 2;
        if (check(mid)) {
            answer = mid;
            right = mid - 1;
        } else {
            left = mid + 1;
        }
    }

    cout << answer << endl;
    return 0;
}
