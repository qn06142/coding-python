int main() {
    int n;
    cin >> n;

    vector<int> a(n), b(n);
    for (int i = 0; i < n; ++i) {
        cin >> a[i];
    }
    for (int i = 0; i < n; ++i) {
        cin >> b[i];
    }

    sort(a.begin(), a.end());
    sort(b.begin(), b.end());

    int scoreT = 0;
    int i = 0; 
    int j = 0; 

    while (i < n && j < n) {
        if (a[i] > b[j]) {

            scoreT++;
            j++; 
        }
        i++; 
    }

    int scoreV = n - scoreT;

    int result = scoreT - scoreV;

    cout << result << endl;

    return 0;
}