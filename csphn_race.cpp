using ll = long long;
const int N = 1e6;
ll n, a[N],b[N];

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cin >> n;
    for(int i = 1; i <= n; i++) cin >> a[i];
    for(int i = 1; i <= n; i++) cin >> b[i];
    sort(a + 1, a + n + 1);
    sort(b + 1, b + n + 1);
    int vua = 0,  tt = 0;
    int i = 1, j = 1, na = n, nb = n;
    while(i <= na && j <= nb)
    {
        if(a[na] > b[nb])
        {
            tt++;
            na--;nb--;
        }
        else if(a[i] > b[j])
        {
            tt++;
            i++; j++;
        }
         else
        {
            if(a[i] < b[nb]) vua++;
            i++; nb--;
        }
    }

    cout << tt - vua;
}
