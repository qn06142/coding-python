int main() {
    int n;
    cin >> n;
    vector<int> A(n);

    for (int i = 0; i < n; ++i) {
        cin >> A[i];
    }

    sort(A.begin(), A.end());

    int max_remainder = 0;

    for (int i = 0; i < n; ++i) {
        int a_i = A[i];

        int low = i + 1, high = n - 1;
        while (low <= high) {
            int mid = low + (high - low) / 2;
            if (A[mid] >= a_i) {
                low = mid + 1;
            } else {
                high = mid - 1;
            }
        }

        if (high >= i + 1) {
            int a_j = A[high];
            int remainder = a_j % a_i;
            if (remainder > max_remainder) {
                max_remainder = remainder;
            }
        }
    }

    cout << max_remainder << endl;

    return 0;
}