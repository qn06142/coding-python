vector<pair<int, int>> stor;
vector<pair<int, int>> adj[(int) 1e6];
int ans[(int) 1e6];
int m, s, t;
int dijktra() {
    priority_queue<pair<int, int>, vector<pair<int, int>>, greater<pair<int, int>>> pq;
    pq.push({0, s});
    ans[s] = 0;
    while(!pq.empty()) {
        pair<int, int> curr = pq.top();
        pq.pop();
        if(ans[curr.second] < curr.first) {
            continue;
        }
        if(curr.second == t) {
            return curr.first;
        }
        for(pair<int, int> i: adj[curr.second]) {
            int u = i.first, v = i.second;
            if(ans[v] > max(ans[curr.second], u)) {
                pq.push({max(ans[curr.second], u), v});
                ans[v] =  max(ans[curr.second], u);
            }
        }
    }
    return 0;
}
int compressnum(int a, vector<int>& cmp) {
    return (int) (lower_bound(cmp.begin(), cmp.end(), a) - cmp.begin() + 1);
}
signed main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);
    cin >> m >> s >> t;
    vector<int> tmp;
    for(int i = 0; i < m; i++) {
        int a, b;
        cin >> a >> b;
        stor.emplace_back(a, b);
        tmp.push_back(a);
        
        tmp.push_back(b);
    }
    tmp.push_back(s);
    tmp.push_back(t);
    vector<int> compress(tmp.begin(), tmp.end());
    sort(compress.begin(), compress.end());
    compress.resize(unique(compress.begin(), compress.end()) - compress.begin());
    for(int i = 0; i <  m; i++) {
        adj[compressnum(stor[i].first, compress)].emplace_back(i + 1, compressnum(stor[i].second, compress));
    }
    s = compressnum(s, compress);
    t = compressnum(t, compress);
    memset(ans, 0x3f, sizeof ans);
    cout << dijktra();
}
