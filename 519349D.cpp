const int N = 105;
const int INF = 1e9;

int d[N][N][2], f[N][N][2], n;
char a[N][N];

int dxKnight[8] = {-1, -1, +1, +1, -2, -2, +2, +2};
int dyKnight[8] = {-2, +2, -2, +2, -1, +1, -1, +1};

bool inside(int x, int y) {
    return x > 0 && x <= n && y > 0 && y <= n;
}

void bfsKnight(int x, int y) {
    for (int i = 1; i <= n; i++)
        for (int j = 1; j <= n; j++)
            for (int k = 0; k <= 1; k++)
                d[i][j][k] = INF;

    d[x][y][0] = 0;
    queue<pair<pair<int, int>, int>> qu;
    qu.push({{x, y}, 0});

    while (!qu.empty()) {
        auto tmp = qu.front();
        qu.pop();
        int x = tmp.first.first, y = tmp.first.second, z = tmp.second;

        for (int k = 0; k < 8; k++) {
            int u = x + dxKnight[k], v = y + dyKnight[k];
            if (inside(u, v) && a[u][v] != '#' && d[u][v][z ^ 1] == INF) {
                d[u][v][z ^ 1] = d[x][y][z] + 1;
                qu.push({{u, v}, z ^ 1});
            }
        }
    }

    for (int i = 1; i <= n; i++)
        for (int j = 1; j <= n; j++)
            for (int k = 0; k <= 1; k++)
                f[i][j][k] = max(f[i][j][k], d[i][j][k]);
}

void bfsKing(int x, int y) {
    int dxKing[8] = {0, 0, -1, 1, -1, -1, +1, +1};
    int dyKing[8] = {-1, 1, 0, 0, -1, +1, -1, +1};

    for (int i = 1; i <= n; i++)
        for (int j = 1; j <= n; j++)
            for (int k = 0; k <= 1; k++)
                d[i][j][k] = INF;

    d[x][y][0] = 0;
    queue<pair<pair<int, int>, int>> qu;
    qu.push({{x, y}, 0});

    while (!qu.empty()) {
        auto tmp = qu.front();
        qu.pop();
        int x = tmp.first.first, y = tmp.first.second, z = tmp.second;

        for (int k = 0; k < 8; k++) {
            int u = x + dxKing[k], v = y + dyKing[k];
            if (inside(u, v) && a[u][v] != '#' && d[u][v][z ^ 1] == INF) {
                d[u][v][z ^ 1] = d[x][y][z] + 1;
                qu.push({{u, v}, z ^ 1});
            }
        }
    }

    for (int i = 1; i <= n; i++)
        for (int j = 1; j <= n; j++)
            for (int k = 0; k <= 1; k++)
                f[i][j][k] = max(f[i][j][k], d[i][j][k]);
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    cin >> n;
    int res = INF;

    for (int i = 1; i <= n; i++)
        for (int j = 1; j <= n; j++)
            cin >> a[i][j];

    for (int i = 1; i <= n; i++) {
        for (int j = 1; j <= n; j++) {
            if (a[i][j] == 'M') {
                bfsKnight(i, j);
            } else if (a[i][j] == 'T') {
                bfsKing(i, j);
            }
        }
    }

    for (int i = 1; i <= n; i++)
        for (int j = 1; j <= n; j++)
            for (int k = 0; k <= 1; k++)
                res = min(res, f[i][j][k]);

    cout << (res == INF ? -1 : res) << "\n";
    return 0;
}
