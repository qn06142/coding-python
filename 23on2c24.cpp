#include <iostream>
#include <vector>
using namespace std;

int N, K;
vector<vector<int>> dp;

bool isValid(int mask, int prevMask) {
    for (int i = 0; i < N; i++) {
        if (mask & (1 << i)) {
            // Check for adjacent kings in the same row
            if ((i > 0 && (mask & (1 << (i - 1)))) || (i < N - 1 && (mask & (1 << (i + 1)))))
                return false;
            // Check for adjacent kings in the previous row
            if ((prevMask & (1 << i)) || (i > 0 && (prevMask & (1 << (i - 1)))) || (i < N - 1 && (prevMask & (1 << (i + 1)))))
                return false;
        }
    }
    return true;
}

int countWays() {
    dp = vector<vector<int>>(N + 1, vector<int>(1 << N, 0));
    dp[0][0] = 1; // Base case: 1 way to arrange 0 kings on 0 rows

    for (int row = 1; row <= N; row++) {
        for (int mask = 0; mask < (1 << N); mask++) {
            for (int prevMask = 0; prevMask < (1 << N); prevMask++) {
                if (isValid(mask, prevMask)) {
                    dp[row][mask] += dp[row - 1][prevMask];
                }
            }
        }
    }

    int totalWays = 0;
    for (int mask = 0; mask < (1 << N); mask++) {
        if (__builtin_popcount(mask) == K) {
            totalWays += dp[N][mask];
        }
    }

    return totalWays;
}

int main() {
    cin >> N >> K;
    int result = countWays();
    cout << result << endl;
    return 0;
}
