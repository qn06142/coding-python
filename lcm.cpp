const int MOD = 1e9 + 7;
const int MAXN = 1e7 + 5;

vector<int> spf(MAXN, 0);  // Smallest prime factor
vector<int> power(MAXN, 0);  // Maximum power of each prime in LCM

void sieve() {
    for (int i = 2; i < MAXN; i++) {
        if (spf[i] == 0) {
            for (int j = i; j < MAXN; j += i) {
                if (spf[j] == 0) {
                    spf[j] = i;
                }
            }
        }
    }
}

long long exp(long long base, long long exponent, int mod = MOD) {
    long long result = 1;
    base %= mod;

    while (exponent > 0) {
        if (exponent % 2 == 1) {
            result = (result * base) % mod;
        }
        base = (base * base) % mod;
        exponent /= 2;
    }

    return result;
}

void updatePower(int x) {
    unordered_map<int, int> factor;
    while (x != 1) {
        factor[spf[x]]++;
        x /= spf[x];
    }
    for (auto &p : factor) {
        power[p.first] = max(power[p.first], p.second);
    }
}

long long getlcmmod(int n) {
    long long ans = 1;
    for (int i = 1; i <= n; i++) {
        updatePower(i);
    }
    for (int i = 2; i < MAXN; i++) {
        if (power[i] > 0) {
            ans = (ans * exp(i, power[i], MOD)) % MOD;
        }
    }
    return ans;
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    
    int n;
    cin >> n;

    sieve();
    cout << getlcmmod(n) << endl;
    return 0;
}
