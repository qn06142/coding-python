int main() {
    int T;
    cin >> T;
    while (T--) {
        int N;
        cin >> N;
        vector<int> A(N);
        for (int i = 0; i < N; ++i) {
            cin >> A[i];
        }
        int Q;
        cin >> Q;
        vector<int> K(Q);
        for (int i = 0; i < Q; ++i) {
            cin >> K[i];
        }

        // Sort the array once
        vector<int> sorted_A = A;
        sort(sorted_A.begin(), sorted_A.end());

        // Process each query
        for (int i = 0; i < Q; ++i) {
            int k = K[i];
            if (N - k < 2) {
                cout << 0 << endl;
                continue;
            }

            // Consider the remaining part of the deck after removing K cards from the bottom
            vector<int> remaining(sorted_A.begin(), sorted_A.end() - k);

            // Count frequencies of each card
            unordered_map<int, int> freq;
            for (int card : remaining) {
                freq[card]++;
            }

            // Calculate the number of ways to win
            int ways = 0;
            for (const auto& pair : freq) {
                int count = pair.second;
                if (count >= 2) {
                    ways += ceil((count * (count - 1)) / 2);
                }
            }

            cout << ways << endl;
        }
    }
    return 0;
}
