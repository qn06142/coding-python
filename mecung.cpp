const int MAXN = 3001;
int n, m;
char mat[MAXN][MAXN];
bool visited[MAXN][MAXN];
int dist[MAXN][MAXN];
vector<int> monsters[MAXN][MAXN];
const int dx[] = {0, 1, -1, 0};
const int dy[] = {1, 0, 0, -1};

inline bool isvalid(int x, int y) {
    return x >= 1 && x <= n && y >= 1 && y <= m;
}

void flood(int i, int j) {
    memset(visited, 0, sizeof(visited));
    memset(dist, -1, sizeof(dist));

    queue<pair<pair<int, int>, int>> q;
    q.push({{i, j}, 0});
    dist[i][j] = 0;

    while (!q.empty()) {
        auto cur = q.front(); 
        q.pop();

        int x = cur.first.first;
        int y = cur.first.second;
        int cur_dist = cur.second;

        if (!isvalid(x, y) || mat[x][y] == '#' || visited[x][y]) continue;

        dist[x][y] = cur_dist;
        visited[x][y] = true;

        monsters[x][y].push_back(cur_dist);

        for (int d = 0; d < 4; d++) {
            int nx = x + dx[d];
            int ny = y + dy[d];
            if (isvalid(nx, ny) && mat[nx][ny] != '#' && dist[nx][ny] == -1) {
                q.push({{nx, ny}, cur_dist + 1});
            }
        }
    }
}

void calcmonster() {
    for (int i = 1; i <= n; i++) {
        for (int j = 1; j <= m; j++) {
            if (mat[i][j] == 'd') {
                flood(i, j);
            }
        }
    }
}

bool check(int startX, int startY, int initialHP) {
    memset(visited, 0, sizeof(visited));

    queue<pair<pair<int, int>, pair<int, int>>> q; 
    q.push({{startX, startY}, {initialHP, 0}}); 
    visited[startX][startY] = true;

    while (!q.empty()) {
        auto cur = q.front();
        q.pop();

        int x = cur.first.first;
        int y = cur.first.second;
        int hp = cur.second.first;
        int dist = cur.second.second;

        if (mat[x][y] == 'e' && hp > 0) {
            int tmp = 0;
            for (int monsterd : monsters[x][y]) {
                if (monsterd < dist) {
                    tmp++;
                }
            }
            if(tmp <= initialHP) {
                debug(cur);
            }
            return tmp < initialHP;
        }

        for (int d = 0; d < 4; d++) {
            int nx = x + dx[d];
            int ny = y + dy[d];

            if (!isvalid(nx, ny) || visited[nx][ny] || mat[nx][ny] == '#') {
                continue;
            }

            int newHP = hp - (mat[nx][ny] == 'd' ? 1 : 0);

            if (newHP <= 0) continue;

            visited[nx][ny] = true;
            q.push({{nx, ny}, {newHP, dist + 1}});
        }
    }

    return false;
}

