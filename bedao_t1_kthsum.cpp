#define int long long
int n, k;
int bit[(int) 1e5 + 5];
int a[(int) 1e5 + 5];
int pref[(int) 1e5 + 5];
void update(int x) {
    for (; x <= n; x += x & (-x))
        bit[x]++;
}

int query(int x) {
    int ans = 0;
    for (; x > 0; x -= x & (-x))
        ans += bit[x];
    return ans; 
}
int check(int x) {
    int cnt = 0;
    int sum = 0;
    memset(bit, 0, sizeof (bit));
    for(int i = 1; i <= n; i++) {
        update(lower_bound(pref + 1, pref + 1 + n, sum) - pref);
        sum += a[i];
        cnt += query(upper_bound(pref + 1, pref + 1 + n, sum - x) - pref - 1);

    }
    return (cnt >= k);
}
signed main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);
    cin >> n >> k;
    for(int i = 1; i <= n; i++) {
        cin >> a[i];
        pref[i] = pref[i - 1] + a[i - 1];
    }
    sort(pref + 1, pref + 1 + n);
    int ans = LLONG_MIN;
    int l = -(1e14 + 5), r = 1e14 + 5;
    while(l <= r) {
        int mid = (l + r) / 2;
        if(check(mid)) {
            ans = mid;
            l = mid + 1;
        } else r = mid - 1;
    }
    cout << ans;
}
