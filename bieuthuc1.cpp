int n;
long long m;
vector<int> A;
vector<string> result;

void bacctracc(int idx, long long current_value, string expression) {
    if (idx == n) {
        if (current_value == m) {
            result.push_back(expression);
        }
        return;
    }
    
    // Add "+"
    bacctracc(idx + 1, current_value + A[idx], expression + (A[idx] >= 0 ? "+" : "-") + to_string(abs(A[idx])));
    
    // Add "-"
    bacctracc(idx + 1, current_value - A[idx], expression +  (A[idx] >= 0 ? "-" : "+")  + to_string(abs(A[idx])));
}

int main() {
    cin >> n >> m;
    A.resize(n);
    for (int i = 0; i < n; ++i) {
        cin >> A[i];
    }
    
    bacctracc(1, A[0], to_string(A[0]));
    
    sort(result.begin(), result.end());
    
    for (const string &expr : result) {
        cout << expr << endl;
    }
    
    return 0;
}
