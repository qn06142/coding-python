int N;
vector<int> a;
vector<vector<int>> ans;

void solve(vector<int>& assignment, int idx, int sum1, int sum2) {
    if (idx == N) {
        if (sum1 == sum2) {
            ans.push_back(assignment);
        }
        return;
    }

    assignment[idx] = 1;
    solve(assignment, idx + 1, sum1 + a[idx], sum2);

    assignment[idx] = 2;
    solve(assignment, idx + 1, sum1, sum2 + a[idx]);
}

int main() {
    cin >> N;
    a.resize(N);
    for (int i = 0; i < N; ++i) {
        cin >> a[i];
    }

    vector<int> assignment(N);
    solve(assignment, 0, 0, 0);

    if (ans.empty()) {
        cout << -1 << endl;
    } else {
        sort(ans.begin(), ans.end());
        for (const auto& sol : ans) {
            for (int x : sol) {
                cout << x;
            }
            cout << endl;
        }
    }

    return 0;
}