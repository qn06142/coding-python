int main() {
    #define int long long
    int N, M;
    cin >> N >> M;
    vector<int> arr(N);
    for (int i = 0; i < N; ++i) {
        cin >> arr[i];
    }

    if (*max_element(arr.begin(), arr.end()) > M) {
        cout << -1 << endl;
        return 0;
    }

    vector<int> dp(N + 1, LLONG_MAX);
    dp[0] = 0;

    for (int i = 1; i <= N; ++i) {
        int current_sum = 0;
        int current_max = 0;
        for (int j = i - 1; j >= 0; --j) {
            current_sum += arr[j];
            current_max = max(current_max, arr[j]);
            if (current_sum <= M) {
                dp[i] = min(dp[i], dp[j] + current_max);
            } else {
                break;
            }
        }
    }

    int result = dp[N];
    if (result == LLONG_MAX) {
        result = -1;
    }
    cout << result << endl;

    return 0;
}