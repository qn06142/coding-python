int main() {
    int n;
    cin >> n;
    vector<int> picks(n);
    for (int i = 0; i < n; ++i) {
        cin >> picks[i];
    }

    stack<int> pot;
    int expected_ball = 1;

    for (int pick : picks) {

        while (expected_ball <= n && (pot.empty() || pot.top() != pick)) {
            pot.push(expected_ball);
            expected_ball++;
        }

        if (!pot.empty() && pot.top() == pick) {
            pot.pop();
        } else {

            cout << "YES" << endl;
            return 0;
        }
        cout << expected_ball << ' ';
    }

    cout << "NO" << endl;
    return 0;
}