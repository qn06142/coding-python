int main() {
    int N;
    cin >> N;
    vector<long long> A(N);
    
    for (int i = 0; i < N; ++i) {
        cin >> A[i];
    }
    
    unordered_map<long long, int> freq;
    
    for (int i = 0; i < N; ++i) {
        long long num = A[i];
        while (num % 2 == 0) {
            num /= 2;
        }
        freq[num]++;
    }
    
    long long k = 0;
    int max_freq = 0;
    
    for (const auto& pair : freq) {
        if (pair.second > max_freq || (pair.second == max_freq && pair.first > k)) {
            k = pair.first;
            max_freq = pair.second;
        }
    }
    
    cout << max_freq << endl;
    return 0;
}
