#define int long long
int a[(int)5e6 + 5];

signed main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);
    
    int n;
    long long sum = 0;
    cin >> n;
    
    for(int i = 1; i <= n; i++) {
        cin >> a[i];
        sum += a[i];
    }
    
    long long ans = sum;
    int l = 1;
    long long currsum = 0;
    
    for(int r = 1; r <= n; r++) {
        currsum += a[r];
        
        while(currsum * 2 > sum && l <= r) {
            currsum -= a[l];
            l++;
        }
        
        ans = min(ans, abs(sum - 2 * currsum));
        
        if (l > 1) {
            ans = min(ans, abs(sum - 2 * (currsum + a[l - 1])));
        }
    }
    
    cout << ans << endl;
    return 0;
}
