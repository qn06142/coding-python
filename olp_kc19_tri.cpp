int main() {
    #define int long long
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);
    int n, k;
    cin >> n >> k;
    vector<int> a(n + 1);
    vector<long long> pref(n + 1);
    for(int i = 1; i <= n; i++) {
        cin >> a[i];
        pref[i] = pref[i - 1] + a[i];
    }
    int ans = 0;
    for(int i = 1; i <= n - k + 1; i++) {
        //cerr << endl;
        long long sum = pref[i + k - 1] - pref[i - 1];
        int l = i, r = i + k - 3;
        int maxa = -1;
        while(l <= r) {
            int mid = (l + r) / 2;
            if(sum > 2 * (pref[mid] - pref[i - 1])) {
                maxa = mid;
                l = mid + 1;
            } else {
                r = mid - 1;
            }
        }
        if(maxa == -1) {
            //cerr << "maxa";
            continue;
        }
        //cerr << sum << endl;
        //cerr << maxa << endl;;
        int maxb = -1;
        l = maxa + 1, r = i + k - 2;
        while(l <= r) {
            int mid = (l + r) / 2;
            //cerr << i + maxa - 1 << ' ' << i + maxa + mid - 1 << endl;;
            //cerr << pref[i + maxa + mid - 1] - pref[i + maxa - 1] << endl;;
            if(sum > 2 * (pref[mid] - pref[maxa])) {
                maxb = mid;
                l = mid + 1;
            } else {
                r = mid - 1;
            }
        }
        if(maxb == -1) {
            //cerr << "maxb" << endl;
            continue;
        }
        //cerr << sum << endl;
        //cerr << 2 * (pref[i + k - 1] - pref[i + maxb + maxa]) << endl;
        //cerr << i + k - 1 << ' ' << i + maxb + maxa << endl;;
        //cerr  << i << ' ' << i + maxa << ' ' << i + maxa + maxb << endl;
        if(sum > 2 * (pref[i + k - 1] - pref[maxb])) ans ++;
        
    }
    cout << ans;
}
