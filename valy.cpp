#define int long long
int w[(int) 100], v[100];
vector<pair<int, int>> s1, s2;
void bacctracc1(int l, int r, int w_, int v_) {
    if(l > r) {
        s1.emplace_back(w_, v_);
        return;
    }
    bacctracc1(l + 1, r, w_ + w[l], v_ + v[l]);
    bacctracc1(l + 1, r, w_, v_);
}
void bacctracc2(int l, int r, int w_, int v_) {
    if(l > r) {
        s2.emplace_back(w_, v_);
        return;
    }
    bacctracc2(l + 1, r, w_ + w[l], v_ + v[l]);
    bacctracc2(l + 1, r, w_, v_);
}
signed main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);
    int n, w_;
    cin >> n >> w_;
    for(int i = 1; i <= n; i++) {
        cin >> w[i];
        cin >> v[i];
    }
    int mid = n / 2;

    bacctracc1(1, mid, 0, 0);
    bacctracc2(mid + 1, n, 0, 0);
    sort(s1.begin(), s1.end());
    sort(s2.begin(), s2.end());
    int x = s1.size(), y = s2.size();
    vector<int> prefmax1(x), prefmax2(y);
    prefmax1[0] = s1[0].second;
    prefmax2[0] = s2[0].second;
    for(int i = 1; i < x; i++) {
        prefmax1[i] = max(prefmax1[i - 1], s1[i].second);
    }
    for(int i = 1; i < y; i++) {
        prefmax2[i] = max(prefmax2[i - 1], s2[i].second);
    }
    int r = y - 1, ans = LLONG_MIN;
    for(int l = 0; l < x; l++) {
        
        while(r >= 0 and s1[l].first + s2[r].first > w_) r--;
        if(r >= 0)
        ans = max(ans, prefmax1[l] + prefmax2[r]);
    }
    cout << ans;
}
