long long count_pairs(long long x) {
    return x * (x - 1) / 2;
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    long long n, m, k;
    cin >> n >> m >> k;

    vector<long long> rows(n, 0), cols(m, 0);
    vector<long long> diag1(n + m - 1, 0);  
    vector<long long> diag2(n + m - 1, 0);  

    for (long long i = 0; i < k; ++i) {
        long long x, y;
        cin >> x >> y;
        --x; --y; 

        rows[x]++;
        cols[y]++;
        diag1[x - y + (m - 1)]++;  
        diag2[x + y]++;
    }

    long long result = 0;

    for (long long count : rows) {
        if (count > 1) {
            result += count_pairs(count);
        }
    }

    for (long long count : cols) {
        if (count > 1) {
            result += count_pairs(count);
        }
    }

    for (long long count : diag1) {
        if (count > 1) {
            result += count_pairs(count);
        }
    }

    for (long long count : diag2) {
        if (count > 1) {
            result += count_pairs(count);
        }
    }

    cout << result << endl;
    return 0;
}