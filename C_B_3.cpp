long long maxScore(vector<int>& A, int N) {
    int len = 3 * N;

    priority_queue<int, vector<int>, greater<int>> minHeap;
    vector<long long> prefixSum(N + 1, 0);

    long long sum = 0;
    for (int i = 0; i < N; ++i) {
        minHeap.push(A[i]);
        sum += A[i];
    }
    prefixSum[0] = sum;

    for (int i = N; i < 2 * N; ++i) {
        minHeap.push(A[i]);
        sum += A[i] - minHeap.top();
        minHeap.pop();
        prefixSum[i - N + 1] = sum;
    }

    priority_queue<int> maxHeap;
    vector<long long> suffixSum(N + 1, 0);

    sum = 0;
    for (int i = len - 1; i >= 2 * N; --i) {
        maxHeap.push(A[i]);
        sum += A[i];
    }
    suffixSum[0] = sum;

    for (int i = 2 * N - 1; i >= N; --i) {
        maxHeap.push(A[i]);
        sum += A[i] - maxHeap.top();
        maxHeap.pop();
        suffixSum[2 * N - i] = sum;
    }

    long long maxScore = LLONG_MIN;
    for (int i = 0; i <= N; ++i) {
        maxScore = max(maxScore, prefixSum[i] - suffixSum[N - i]);
    }

    return maxScore;
}

int main() {
    int N;
    cin >> N;
    vector<int> A(3 * N);
    for (int i = 0; i < 3 * N; ++i) {
        cin >> A[i];
    }
    cout << maxScore(A, N) << endl;
    return 0;
}