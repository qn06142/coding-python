vector<int> a;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);
    
    int n, q;
    cin >> n >> q;
    
    a.resize(n);
    for (int i = 0; i < n; ++i) {
        cin >> a[i];
    }
    sort(a.begin(), a.end());
    vector<int> updates;
    int blocksize = sqrt(n);
    long long prev = 0;
    int query_count = 0;

    while (q--) {
        int t, x_prime;
        cin >> t >> x_prime;

        long long x = (x_prime * (prev + 1)) % (int(1e9) + 7);

        if (t == 1) {
            updates.push_back(x);
            query_count++;
            if (query_count == blocksize) {
                // Merge updates into main array
                for (auto i : updates) {
                    a.push_back(i);
                }
                updates.clear();
                sort(a.begin(), a.end());
                query_count = 0;
            }
        } else if (t == 2) {
            int count = a.end() - lower_bound(a.begin(), a.end(), x);
            for (auto i : updates) {
                if (i >= x) {
                    ++count;
                }
            }
            cout << count << '\n';
            prev = count;
        }
    }

    return 0;
}
