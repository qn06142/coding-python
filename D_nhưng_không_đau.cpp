struct node {
    int a[5];
};
int visited[30][30][30][30];
const int delta = 10;
void bfs() {
    queue<node> que;
    que.push({0, 0, 0, 0, 0});
    visited[10][10][10][10] = 0;
    while(!que.empty()) {
        node curr = que.front();
        que.pop();
        for(int l = 1; l <= 4; l++) {
            for(int r = l; r <= 4; r++) {

                for(int i = -1; i <= 1; i++) {
                    node cp = curr;
                    if(i == 0) continue;
                    for(int j = l; j <= r; j++) {
                        cp.a[j] += i;
                        cp.a[j] %= 10;
                    }
                    if(visited[cp.a[1] + delta][cp.a[2] + delta][cp.a[3] + delta][cp.a[4] + delta] == -1) {
                        visited[cp.a[1] + delta][cp.a[2] + delta][cp.a[3] + delta][cp.a[4] + delta] = visited[curr.a[1] + delta][curr.a[2] + delta][curr.a[3] + delta][curr.a[4] + delta] + 1;
                        que.push(cp);
                    }
                }
            }
        }
    }
}
int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);
    int n;
    cin >> n;
    memset(visited, -1, sizeof(visited));
    bfs();
    while(n--) {
        string a, b;
        cin >> a >> b;
        node tmp;
        for(int i = 1; i <= 4; i++) {
            tmp.a[i] = b[i - 1] - a[i - 1] + delta;
        }
        cout << visited[tmp.a[1]][tmp.a[2]][tmp.a[3]][tmp.a[4]] << '\n';
    }
}
