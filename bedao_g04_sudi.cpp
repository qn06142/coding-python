signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int N;
    cin >> N;
    vector<int> a(N);
    for (int i = 0; i < N; ++i) {
        cin >> a[i];
    }

    vector<int> left_max(N), right_max(N), left_min(N), right_min(N);
    stack<int> s;

    for (int i = 0; i < N; ++i) {
        while (!s.empty() && a[s.top()] < a[i]) s.pop();
        left_max[i] = s.empty() ? -1 : s.top();
        s.push(i);
    }
    while (!s.empty()) s.pop();
    for (int i = N - 1; i >= 0; --i) {
        while (!s.empty() && a[s.top()] <= a[i]) s.pop();
        right_max[i] = s.empty() ? N : s.top();
        s.push(i);
    }
    while (!s.empty()) s.pop();

    for (int i = 0; i < N; ++i) {
        while (!s.empty() && a[s.top()] > a[i]) s.pop();
        left_min[i] = s.empty() ? -1 : s.top();
        s.push(i);
    }
    while (!s.empty()) s.pop();
    for (int i = N - 1; i >= 0; --i) {
        while (!s.empty() && a[s.top()] >= a[i]) s.pop();
        right_min[i] = s.empty() ? N : s.top();
        s.push(i);
    }

    int total_max = 0, total_min = 0;
    for (int i = 0; i < N; ++i) {
        total_max += a[i] * (i - left_max[i]) * (right_max[i] - i);
        total_min += a[i] * (i - left_min[i]) * (right_min[i] - i);
    }

    cout << total_max - total_min << "\n";
    return 0;
}