#pragma GCC optimization("unroll-loops,fast-math,O3")
const int maxprime = 7e7;
void sieve(vector<int>& primes) {
    vector<bool> is_prime(maxprime + 1, true);
    for (int p = 2; p <= (int) sqrt(maxprime); ++p) {
        if (is_prime[p]) {
            for (int i = p * p; i <= maxprime; i += p) {
                is_prime[i] = false;
            }
        }
    }
    for (int p = 2; p <= maxprime; ++p) {
        if (is_prime[p]) {
            primes.push_back(p);
        }
    }
}

vector<long long> factorize(long long n, const vector<int>& primes) {
    vector<long long> factors;
    for (int prime : primes) {
        if (prime * prime > n) break;
        if (n % prime == 0) {
            while (n % prime == 0) {
                factors.push_back(prime);
                n /= prime;
            }
        }
    }
    if (n > 1) { 
        factors.push_back(n);
    }
    return factors;
}

int main() {
    long long N;
    cin >> N;

    vector<int> primes;
    sieve(primes); 

    vector<long long> factors = factorize(N, primes);

    if (factors.size() == 6) {
        sort(factors.begin(), factors.end());
        for (long long factor : factors) {
            cout << factor << " ";
        }
        cout << endl;
    } else {
        cout << "-1" << endl;
    }

    return 0;
}