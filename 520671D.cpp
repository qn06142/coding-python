const int N = 1e5 + 7;

int a[N], n, k;
int Max[N][21], p[21], l2[N];

void prep() {
    p[0] = 1;
    for (int i = 1; i <= 20; i++) p[i] = p[i - 1] * 2;
    for (int i = n; i > 0; i--) {
        Max[i][0] = a[i];
        for (int j = 1; i + p[j] - 1 <= n; j++)
            Max[i][j] = max(Max[i][j - 1], Max[i + p[j - 1]][j - 1]);
    }
    l2[1] = 0;
    for (int i = 1; i <= n; i++) {
        l2[i] = l2[i - 1];
        if (p[l2[i]] * 2 <= i) l2[i]++;
    }
}

int query(int l, int r) {
    int v = l2[r - l + 1];
    return max(Max[l][v], Max[r - p[v] + 1][v]);
}

int main() {
    ios_base::sync_with_stdio(0);
    cin.tie(0);
    cin >> n >> k;
    for (int i = 1; i <= n; i++) cin >> a[i];
    prep();
    for (int i = 1; i <= n; i++) {
        int L = -1, R = -1;
        int d = 1, c = i, g;
        while (d <= c) {
            g = (d + c) >> 1;
            if (query(g, i) <= a[i] + k) L = g, c = g - 1;
            else d = g + 1;
        }
        d = i, c = n;
        while (d <= c) {
            int g = (d + c) >> 1;
            if (query(i, g) <= a[i] + k) R = g, d = g + 1;
            else c = g - 1;
        }
        cout << R - L + 1 << " ";
    }
    return 0;
}
