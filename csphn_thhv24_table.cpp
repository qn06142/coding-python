using namespace std;

int mat[405][405];

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    int n, m, k;
    cin >> n >> m >> k;
    for(int i = 1; i <= n; i++) {
        for(int j = 1; j <= m; j++) {
            cin >> mat[i][j];
        }
    }

    long long ans = 0;
    for(int l = 1; l <= m; l++) {
        vector<int> tmp(n + 1, 0);
        for(int r = l; r <= m; r++) {
            for(int i = 1; i <= n; i++) {
                tmp[i] += mat[i][r];
            }
            vector<int> mod(k + 1, 0);
            long long sum = 0;
            mod[0] = 1; // there's always one subarray with sum 0

            int count = 0;
            for(int i = 1; i <= n; i++) {
                sum += tmp[i];
                int mod_val = ((sum % k) + k) % k;
                count += mod[mod_val];
                mod[mod_val]++;
            }
            ans += count;
        }
    }

    cout << ans << '\n';
    return 0;
}
