const int N = 107;

int c[N][N], n, m;
int d[N], par[N];

vector<int> shortest_path(int s, int t) {
    for (int i = 1; i <= n; i++) {
        d[i] = 2e9;
        par[i] = 0;
    }
    
    priority_queue<pair<int, int>, vector<pair<int, int>>, greater<pair<int, int>>> pq;
    d[s] = 0;
    pq.push({d[s], s});
    
    while (!pq.empty()) {
        int w = pq.top().first;
        int u = pq.top().second;
        pq.pop();
        
        if (w > d[u]) continue;
        
        for (int x = 1; x <= n; x++) {
            if (c[u][x] > 0 && d[x] > w + c[u][x]) {
                d[x] = w + c[u][x];
                par[x] = u;
                pq.push({d[x], x});
            }
        }
    }
    
    vector<int> node;
    while (t > 0) {
        node.push_back(t);
        t = par[t];
    }
    reverse(node.begin(), node.end());
    return node;
}

int dijkstra(int s, int t) {
    for (int i = 1; i <= n; i++) {
        d[i] = 2e9;
        par[i] = 0;
    }
    
    priority_queue<pair<int, int>, vector<pair<int, int>>, greater<pair<int, int>>> pq;
    d[s] = 0;
    pq.push({d[s], s});
    
    while (!pq.empty()) {
        int w = pq.top().first;
        int u = pq.top().second;
        pq.pop();
        
        if (w > d[u]) continue;
        
        for (int x = 1; x <= n; x++) {
            if (c[u][x] > 0 && d[x] > w + c[u][x]) {
                d[x] = w + c[u][x];
                par[x] = u;
                pq.push({d[x], x});
            }
        }
    }
    
    return d[t];
}

