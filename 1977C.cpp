int calc(vector<pair<int, int>> &t, int d) {
    int LCM = 0, cnt = 0;
    for (auto [j, c]: t) {
        if (d % j == 0) {
            if (LCM == 0) LCM = j;
            else LCM = lcm(LCM, j);
            cnt += c;
        }
    }
    if (LCM != d) cnt = 0;
    return cnt;
}
void solve() { 
    int n;
    cin >> n;
    vector<int> a(n);
    for (auto &i: a) cin >> i;
    long long LCM = 1;
    int mx = *max_element(a.begin(), a.end());
    for (auto i: a) {
        LCM = lcm(LCM, i);
        if (LCM > mx) {
            cout << n << '\n';
            return;
        }
    }
    map<int, int> cnt;
    for(auto i:a) {
        cnt[i] ++;
    }
    vector<pair<int, int>> p;
    for(auto i:cnt) {
        p.push_back(i);
    }
    int ans = 0;
    for(int i = 1; i * i <= mx; i++) {
        if (mx % i == 0) {
            if (cnt.find(i) == cnt.end())
                ans = max(ans, calc(p, i));
            if (cnt.find(mx / i) == cnt.end())
                ans = max(ans, calc(p, mx / i));
        }
    }
    cout << ans << '\n';
    
}
int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);
    int q;
    cin >> q;
    while(q--) {
        solve();
    }
}
