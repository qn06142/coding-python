const int INF = 1e9;
const int directions[3][2] = {{1, 0}, {0, 1}, {1, 1}}; 

bool in_bounds(int x, int y, int w, int h) {
    return 0 <= x && x <= w && 0 <= y && y <= h;
}

int min_jumps(int w, int h, int x_start, int y_start, int x_end, int y_end, int n, vector<pair<int, int>>& points) {

    set<pair<int, int>> special_points(points.begin(), points.end());

    queue<tuple<int, int, int, int>> q; 
    set<tuple<int, int, int>> visited;  

    q.push({x_start, y_start, 1, 0});
    visited.insert({x_start, y_start, 1});

    while (!q.empty()) {
        auto [x, y, d, steps] = q.front(); q.pop();

        if (x == x_end && y == y_end) {
            return steps;
        }

        for (const auto& dir : directions) {
            int dx = dir[0], dy = dir[1];
            int nx = x + dx * d, ny = y + dy * d;

            if (!in_bounds(nx, ny, w, h)) {
                continue;
            }

            vector<int> new_ds = (special_points.find({nx, ny}) != special_points.end()) ?
                                 vector<int>{2 * d, d / 2, 1} : vector<int>{d};

            for (int new_d : new_ds) {
                if (visited.find({nx, ny, new_d}) == visited.end()) {
                    visited.insert({nx, ny, new_d});
                    q.push({nx, ny, new_d, steps + 1});
                }
            }
        }
    }

    return -1; 
}

int main() {
    int w, h, x_start, y_start, x_end, y_end, n;
    cin >> w >> h;
    cin >> x_start >> y_start >> x_end >> y_end;
    cin >> n;

    vector<pair<int, int>> points(n);
    for (int i = 0; i < n; ++i) {
        cin >> points[i].first >> points[i].second;
    }

    int result = min_jumps(w, h, x_start, y_start, x_end, y_end, n, points);
    cout << result << endl;

    return 0;
}