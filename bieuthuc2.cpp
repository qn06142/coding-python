long long evall(const string &expression) {

    vector<long long> values;
    vector<char> operators;
    string num = "";

    for (char c : expression) {
        if (c == '+' || c == '-' || c == '*') {
            values.push_back(stoll(num));
            operators.push_back(c);
            num = "";
        } else {
            num += c;
        }
    }
    values.push_back(stoll(num));

    for (int i = 0; i < operators.size(); ++i) {
        if (operators[i] == '*') {
            values[i] = values[i] * values[i + 1];
            values.erase(values.begin() + i + 1);
            operators.erase(operators.begin() + i);
            --i;
        }
    }

    long long result = values[0];
    for (int i = 0; i < operators.size(); ++i) {
        if (operators[i] == '+') {
            result += values[i + 1];
        } else if (operators[i] == '-') {
            result -= values[i + 1];
        }
    }

    return result;
}

void bacctracc(const vector<int> &nums, int index, string expression, long long target, vector<string> &results) {
    if (index == (int) nums.size()) {
        if (evall(expression) == target) {
            results.push_back(expression);
        }
        return;
    }

    bacctracc(nums, index + 1, expression + "+" + to_string(nums[index]), target, results);
    bacctracc(nums, index + 1, expression + "-" + to_string(nums[index]), target, results);
    bacctracc(nums, index + 1, expression + "*" + to_string(nums[index]), target, results);
}

int main() {
    int n;
    long long m;
    cin >> n >> m;

    vector<int> nums(n);
    for (int i = 0; i < n; ++i) {
        cin >> nums[i];
    }

    vector<string> results;
    bacctracc(nums, 1, to_string(nums[0]), m, results);

    sort(results.begin(), results.end());

    for (const string &expr : results) {
        cout << expr << endl;
    }

    return 0;
}