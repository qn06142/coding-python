#define int long long

const int MAXN = (int)1e6 + 5;
int a[MAXN];
long long dp[MAXN], pref[MAXN];
int n, k;
int prev_[MAXN]; 

int cal_value(int i) {
    return (i == 1 ? 0 : dp[i - 2]) - pref[i - 1];
}

signed main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);

    cin >> n >> k;
    k--;
    for(int i = 1; i <= n; i++) {
        cin >> a[i];
        pref[i] = pref[i - 1] + a[i];
    }

    dp[0] = 0;
    deque<int> dq;
    for(int i = 1; i <= n; i++) {
        int val = cal_value(i);
        while(!dq.empty() && dq.front() < i - k + 1) dq.pop_front();
        while(!dq.empty() && cal_value(dq.back()) < val) dq.pop_back();
        dq.push_back(i);

        dp[i] = dp[i - 1];
        prev_[i] = i - 1; 
        if (!dq.empty()) {
            int opt = cal_value(dq.front()) + pref[i];
            if (dp[i] < opt) {
                dp[i] = opt;
                prev_[i] = dq.front() - 2;
            }
        }
    }
    int ans = LLONG_MIN;
    int ansind;
    for(int i = 1; i <= n; i++) {
        if(ans < dp[i]) {
            ans = max(ans, dp[i]);
            ansind = i;
        }
    }
    vector<int> out;
    int idx = ansind;
    while(idx > 0) {
        for(int i = idx; i >= prev_[idx] + 2; i--)
        out.push_back(i);
        idx = prev_[idx];
    }
    cout << out.size() << ' ' << ans << endl;;
    reverse(out.begin(), out.end());
    for(auto i:out) cout << i << ' ';
}