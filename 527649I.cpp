const int LIM = 3010;
int a[LIM][LIM], b[LIM][LIM];
int r, c, h, w;

bool check(int value, int target) {
    for (int i = 1; i <= r; ++i) {
        for (int j = 1; j <= c; ++j) {
            b[i][j] = b[i - 1][j] + b[i][j - 1] - b[i - 1][j - 1];
            b[i][j] += (a[i][j] >= value);
        }
    }

    for (int lx = 1, rx = h; rx <= r; ++lx, ++rx) {
        for (int ly = 1, ry = w; ry <= c; ++ly, ++ry) {
            int cnt = b[rx][ry] - b[lx - 1][ry] - b[rx][ly - 1] + b[lx - 1][ly - 1];
            if (cnt > target) return true;
        }
    }

    return false;
}

int find_min_median() {
    int expected = (h * w + 1) / 2;
    int lower = expected, upper = r * c - expected + 1;



    int res = upper;
    while (lower <= upper) {
        int mid = (lower + upper) / 2;
        if (check(mid, expected)) {
            res = mid;
            lower = mid + 1;
        } else {
            upper = mid - 1;
        }
    }
    
    return res;
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    cin >> r >> c >> h >> w;

    for (int i = 1; i <= r; ++i) {
        for (int j = 1; j <= c; ++j) {
            cin >> a[i][j];
        }
    }

    int result = find_min_median();
    cout << result << endl;

    return 0;
}
