const int BLOCKSIZE = 10;

using namespace std;
struct Node {
    vector<int> nxt; // points to another index in stor1
    int a; //elem at currpos
    long long sum; // stores sum from currpos -> currpos + BLOCKSIZE
    Node () {
        a = 0;
        sum = 0;
        nxt = vector<int>(2, -1);
    }
};
int cnt = 2; // 0 a[1] a[2] ... a[cnt - 2] 1
Node stor[(int) 2e5 + 5];
int cur_size = 2;
int size() {
    return cur_size;
}
int getPos(int pos) { // traces the pointer of pos
    int i = 0;
    int cnt = 0;
    while(cnt < pos) {
        if (cnt + BLOCKSIZE <= pos) {
            cnt += BLOCKSIZE;
            i = stor[i].nxt[1];
        }
        else {
            ++cnt;
            i = stor[i].nxt[0];
        }
    }
    return i;
}
int findth(int i) {
    int ans = 0;
    while (stor[i].nxt[0] != -1) {
        if(stor[i].nxt[1] != -1) {
            ans += BLOCKSIZE;
            i = stor[i].nxt[1];
        } else {
            ans++;
            i = stor[i].nxt[0];
        }
    }
    return size() - ans;
}
long long getSum(int x) {
    long long ans = 0;
    int i = 0, cnt = 0;
    while(cnt < x) {
        if(cnt + BLOCKSIZE <= x) {
            ans += stor[i].sum;
            i = stor[i].nxt[1];
            cnt += BLOCKSIZE;
        } else {
            ans += stor[i].a;
            i = stor[i].nxt[0];
            ++cnt;
        }
    }
    ans += stor[i].a;
    return ans;
}
int newNode(int val) {
    stor[cnt].a = val;
    return cnt++;
}
void updB(int m) {
    int pos = max(0, findth(m) - BLOCKSIZE);
    pos = getPos(pos);
    int tmp = pos;
    long long sum = 0;
    for (int i = 0; i < BLOCKSIZE; ++i) {
        sum += stor[tmp].a;
        tmp = stor[tmp].nxt[0];
        if (tmp == -1) return;
    }
    while (tmp != -1) {
        stor[pos].nxt[1] = tmp;
        stor[pos].sum = sum;
        if (pos == m) break;
        sum -= stor[pos].a;
        sum += stor[tmp].a;
        pos = stor[pos].nxt[0];
        tmp = stor[tmp].nxt[0];
    }
}
void add(int r, int val) {
    int l = getPos(r - 1);
    r = getPos(r);
    int m = newNode(val);
    stor[l].nxt[0] = m;
    stor[m].nxt[0] = r;
    updB(m);
    ++cur_size;
}
void del(int r) {
    int l = getPos(r - 2);
    r = getPos(r);
    stor[l].nxt[0] = r;
    updB(l);
    --cur_size;
}
int a[(int)1e5 + 5];
int main() {
    ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
    int n, q;
    cin >> n >> q;
    stor[0].nxt[0] = 1;
    for (int i = 1; i <= n; ++i) {
        cin >> a[i];
        add(size() - 1, a[i]);
    }
    int cursor = size() - 1;
    while (q--) {
        char c;
        cin >> c;
        // cerr << "DB>> " << c << '\n';
        if(c == 'A') {
            int x;
            cin >> x;
            add(cursor++, x);
        } else if(c == 'L') {
            if(cursor > 1) cursor--;
        } else if(c == 'R') {
            if(cursor + 1 < size()) cursor++;
        } else if(c == 'D') {
            if(cursor > 1) {
                del(cursor--);
            }
        } else if(c == 'Q') {
            int l, r;
            cin >> l >> r;
            l = max(1, l);
            r = min(size() - 1, r);
            // cerr << "DB>> " << l << ' ' << r << '\n';
            if (l <= r) cout << getSum(r) - getSum(l - 1) << '\n';
            // cerr << "ok\n";
        } else {
            return 1;
        }
    }
    return 0;
}
