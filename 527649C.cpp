const int maxn = 1e3 + 5;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);
    int n, m;
    cin >> n >> m;
    int ans = 0;
    vector<int> heights(m + 1);
    for(int i_ = 1; i_ <= n; i_++) {
        for(int i = 1; i <= m; i++) {
            char tmp;
            cin >> tmp;
            if(tmp == '.') {
                heights[i]++; 
            } else {
                heights[i] = 0;
            }
        }
        stack<int> stacc1, stacc2;
        vector<int> r(m + 1), l(m + 1);
        for(int i = 1; i <= m; i++) {
            while(!stacc1.empty() and heights[stacc1.top()] >= heights[i]) {
                stacc1.pop();
            }
            l[i] = stacc1.empty() ? 1 : stacc1.top() + 1;
            stacc1.push(i);
        }
        for(int i = m; i >= 1; i--) {
            while(!stacc2.empty() and heights[stacc2.top()] >= heights[i]) {
                stacc2.pop();
            }
            r[i] = stacc2.empty() ? m : stacc2.top() - 1;
            stacc2.push(i);
        }
        for(int i = 1; i <= m; i++) {
            ans = max(ans, (heights[i] + (r[i] - l[i] + 1)) * 2);
        }
    }
    cout << ans;
}
