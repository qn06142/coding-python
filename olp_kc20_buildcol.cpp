#define int long long
int n, m;
int a[(int) 2e5 + 5], b[(int) 2e5 + 5];

bool check(int x) {
    long long ans1 = 0;
    for(int i = 2; i <= n - 1; i++) {
        if (a[i] >= x) {
            ans1 += b[i];
        } else if (a[i] + b[i] >= x) {
            ans1 += (a[i] + b[i] - x);
        }
    }
    return ans1 >= m;
}

signed main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    cin >> n >> m;
    for(int i = 1; i <= n; i++) {
        cin >> a[i];
    }

    int max_ = a[1];
    for(int i = 2; i <= n; i++) {
        if(a[i] > max_) {
            max_ = a[i];
        } else {
            b[i] = max_ - a[i];
        }
    }
    b[n] = 0;
    max_ = a[n];
    for(int i = n - 1; i >= 1; i--) {
        if(a[i] > max_) {
            max_ = a[i];
            b[i] = 0;
        } else {
            b[i] = min(max_ - a[i], b[i]);
        }
    }
    b[1] = 0;

    int l = 0, r = 1e10, ans = -1;
    while(l <= r) {
        int mid = (l + r) / 2;
        if(check(mid)) {
            cerr << mid << endl;
            ans = mid;
            l = mid + 1;
        } else {
            r = mid - 1;
        }
    }

    cout << ans;
    return 0;
}
