const int N = 5e5 + 5;
int n, q, a[N], b[N];
long long pref1[N], pref2[N], prefmax[N];

int main() {
    ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);

    cin >> n >> q;

    for (int i = 1; i <= n; ++i) {
        cin >> a[i];
        pref1[i] = pref1[i - 1] + a[i];
    }

    for (int i = 1; i <= n; ++i) {
        cin >> b[i];
        pref2[i] = pref2[i - 1] + b[i];
    }

    for (int i = 1; i <= n; ++i) {
        prefmax[i] = max(prefmax[i - 1], a[i] + pref2[i - 1]);
    }

    while (q--) {
        long long k;
        cin >> k;
        int l = 1, r = n, ans = 0;

        while (l <= r) {
            int mid = l + (r - l) / 2;
            if (k >= prefmax[mid]) {
                ans = mid;
                l = mid + 1;
            } else {
                r = mid - 1;
            }
        }

        cout << pref1[ans] << '\n';
    }

    return 0;
}