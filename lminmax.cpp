int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int n;
    long long delta;
    cin >> n >> delta;

    vector<int> a(n);
    for (int i = 0; i < n; ++i) {
        cin >> a[i];
    }

    deque<int> dq1, dq2;
    int left = 0, maxLength = 0;

    for (int right = 0; right < n; ++right) {
        while (!dq1.empty() && a[dq1.back()] <= a[right]) {
            dq1.pop_back();
        }
        dq1.push_back(right);
        while (!dq2.empty() && a[dq2.back()] >= a[right]) {
            dq2.pop_back();
        }
        dq2.push_back(right);

        while (a[dq1.front()] - a[dq2.front()] > delta) {
            if (dq1.front() == left) {
                dq1.pop_front();
            }
            if (dq2.front() == left) {
                dq2.pop_front();
            }
            ++left;
        }

        maxLength = max(maxLength, right - left + 1);
    }

    cout << maxLength << "\n";
    return 0;
}