int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    int n, m;
    cin >> n >> m;

    vector<bool> visited(10001, false); 

    queue<pair<int, int>> que;
    que.push({n, 0});
    visited[n] = true;

    while (!que.empty()) {
        auto curr = que.front();
        que.pop();

        int num = curr.first;
        int steps = curr.second;

        if (num == m) {
            cout << steps;
            return 0;
        }

        if (num < 0 || num > 10000) continue; 

        if (num * 2 <= 10000 && !visited[num * 2]) {
            que.push({num * 2, steps + 1});
            visited[num * 2] = true;
        }

        if (num - 1 >= 0 && !visited[num - 1]) {
            que.push({num - 1, steps + 1});
            visited[num - 1] = true;
        }
    }

    cout << -1;
    return 0;
}