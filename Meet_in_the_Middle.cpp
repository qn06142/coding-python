#pragma GCC optimize("Ofast,unroll-loops,fast-math")
#define int long long
int a[100];
void bacctracc(int l, int r, int sum, vector<int> &c) {
    if(l > r) {
        c.push_back(sum);
        return;
    }
    bacctracc(l + 1, r, sum + a[l], c);
    bacctracc(l + 1, r, sum, c);
}

signed main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);
    int n, x;
    cin >> n >> x;
    for(int i = 1; i <= n; i++) {
        cin >> a[i];
    }
    vector<int> cnt1, cnt2;
    int mid = n / 2;

    bacctracc(1, mid, 0, cnt1);
    bacctracc(mid + 1, n, 0, cnt2);
    sort(cnt1.begin(), cnt1.end());
    sort(cnt2.begin(), cnt2.end());
    int x_ = cnt1.size(), y = cnt2.size();
    int r = y - 1, r1 = y - 1;
    int ans = 0;
    for(int l = 0; l < x_; l++) {
        while(r >= 0 and cnt1[l] + cnt2[r] > x) r--;
        while(r1 >= 0 and cnt1[l] + cnt2[r1] >= x) r1--;
        ans += r - r1;
    }
    cout << ans;
}
