const int LIM = 17;
const int N = 2e5 + 7;

struct Node {
    int child[2], cnt;
    Node() : child{-1, -1}, cnt(0) {}
} node[20000007];

int numNode = 0;
int a[N], q, n;
int bit[N];

inline void add_num(int root, int x) {
    int p = root;
    for (int i = LIM; i >= 0; i--) {
        int bit = (x >> i) & 1;
        if (node[p].child[bit] == -1) node[p].child[bit] = ++numNode;
        p = node[p].child[bit];
        node[p].cnt++;
    }
}

inline void del_num(int root, int x) {
    int p = root;
    for (int i = LIM; i >= 0; i--) {
        int bit = (x >> i) & 1;
        p = node[p].child[bit];
        node[p].cnt--;
    }
}

inline int Count_LEQ(int root, int x) {
    int p = root;
    int res = 0;
    for (int i = LIM; i >= 0; i--) {
        int bit = (x >> i) & 1;
        if (bit == 1) {
            if (node[p].child[0] != -1) res += node[node[p].child[0]].cnt;
            if (node[p].child[1] == -1) return res;
            p = node[p].child[1];
        } else {
            if (node[p].child[0] == -1) return res;
            p = node[p].child[0];
        }
    }
    res += node[p].cnt;
    return res;
}

void Init() {
    for (int i = 1; i <= n; i++) {
        bit[i] = ++numNode;
    }
}

inline void fenwick_tree_add(int p, int x) {
    for (; p <= n; p += p & -p) add_num(bit[p], x);
}

inline void fenwick_tree_del(int p, int x) {
    for (; p <= n; p += p & -p) del_num(bit[p], x);
}

inline int fenwick_tree_get(int p, int x) {
    int res = 0;
    for (; p > 0; p -= p & -p) {
        res += Count_LEQ(bit[p], x);
    }
    return res;
}

inline int fenwick_tree_get(int l, int r, int x) {
    return fenwick_tree_get(r, x) - fenwick_tree_get(l - 1, x);
}

int main() {
    ios_base::sync_with_stdio(0);
    cin.tie(0);

    cin >> n >> q;
    for (int i = 1; i <= n; i++) cin >> a[i];

    Init();
    for (int i = 1; i <= n; i++) fenwick_tree_add(i, a[i]);

    while (q--) {
        int t, p, x, l, r;
        cin >> t;
        if (t == 1) {
            cin >> p >> x;
            fenwick_tree_del(p, a[p]);
            fenwick_tree_add(p, x);
            a[p] = x;
        } else {
            cin >> l >> r >> x;
            cout << fenwick_tree_get(l, r, x) << "\n";
        }
    }

    return 0;
}
