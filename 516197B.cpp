const int N = 1e3 + 7;
const int mod = 1e9 + 7;

long long dp[N][N];
int n, k, cnt[N];

string s[N];

long long fact[N], inv_fact[N];

bool visited[N];

long long mul(long long a, long long b) {
    if (b == 0) return 1;
    long long tmp = mul(a, b / 2);
    tmp = tmp * tmp % mod;
    if (b & 1) tmp = tmp * a % mod;
    return tmp;
}

void build_fact(int n) {
    fact[0] = inv_fact[0] = 1;
    for (int i = 1; i <= n; i++) fact[i] = fact[i - 1] * i % mod;
    inv_fact[n] = mul(fact[n], mod - 2);
    for (int i = n - 1; i >= 1; i--) inv_fact[i] = inv_fact[i + 1] * (i + 1) % mod;
}

long long C(int k, int n) {
    if (k > n) return 0;
    return fact[n] * inv_fact[k] % mod * inv_fact[n - k] % mod;
}

int main() {
    ios_base::sync_with_stdio(0);
    cin.tie(0);
    cin>>n>>k;
    for (int i = 1; i <= n; i++) {
        cin>>s[i];
        sort(s[i].begin(), s[i].end());
    }
    int d = 0;
    for (int i = 1; i <= n; i++)
        if (!visited[i]) {
            ++d;
            for (int j = i; j <= n; j++)
                if (s[i] == s[j]) {
                    visited[j] = true;
                    cnt[d]++;
                }
        }
    build_fact(1000);
    n = d;
    dp[0][0] = 1;
    for (int i = 1; i <= n; i++)
        for (int j = 0; j <= k; j++) {
            dp[i][j] = dp[i - 1][j];
            for (int k = 1; k <= cnt[i]; k++)
                if (k * (k - 1) / 2 <= j) dp[i][j] = (1LL * dp[i][j] + C(k, cnt[i]) * dp[i - 1][j - k * (k - 1) / 2]) % mod;
        }
    cout<<dp[n][k];
    return 0;
}
