bool mat[105][105], visited[105][105];
const int dx[] = {1, -1, -1, 1};
const int dy[] = {-1, -1, 1, 1};\
void bfs(int x, int y) {
    queue<pair<int, int>> que;
    que.push(make_pair(x, y));
    visited[x][y] = true;
    while(!que.empty()) {
        auto curr = que.front();
        que.pop();
        for(int dir = 0; dir < 4; dir++) {
            int x_ = curr.first + dx[dir], y_ = curr.second + dy[dir];
            if(!visited[x_][y_] and mat[x_][y_]) {
                que.push(make_pair(x_, y_));
                visited[x_][y_] = true;
            }
        }
    }
}
int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);
    int n, m;
    cin >> n >> m;
    for(int i = 1; i <= n; i++) {
        for(int j = 1; j <= m; j++) {
            char a;
            cin >> a;
            mat[i][j] = (a == '*');
        }
    }

    int ans = 0;
    for(int i = 1; i <= n; i++) {
        for(int j = 1; j <= m; j++) {
            if(!visited[i][j] and mat[i][j]) {
                ans++;
                bfs(i, j);
            }
        }
    }

    cout << ans;
}
