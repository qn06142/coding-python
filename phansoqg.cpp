#define double long double
double a[40];
int n;
double last;
int priority(char x) {
    switch (x) {
        case '+': return 0;
        case '-': return 0;
        case 'X': return 1;
    }
    return 0;
}
double process(char op, double a, double b) {
    if(op == '+') return a + b;
    if(op == '-') return a - b;
    if(op == 'X') return a * b;
    return 0;
}

bool calc(string op) {
    stack<double> vals;
    stack<char> ops;
    int currelem = 1;
    for(int i = 0; i < op.size(); i++) {
        vals.push(a[currelem++]);

        char currop = op[i];
        while(!ops.empty() and priority(ops.top()) >= priority(currop)) {
            double x = vals.top(); vals.pop();
            double y = vals.top(); vals.pop();
            vals.push(process(ops.top(), y, x));
            ops.pop();
        }
        ops.push(currop);
    }
    vals.push(a[currelem++]);
    while(!ops.empty()) {
        double x = vals.top(); vals.pop();
        double y = vals.top(); vals.pop();
        vals.push(process(ops.top(), y, x));
        ops.pop();
        
    }
    return fabs(vals.top() - last) <= 1e-6;
}

void bacctracc(string op) {
    if(op.size() == n - 2) {
        if(calc(op)) {
            cout << op << endl;
            exit(0);
        }
        return;
    }
    bacctracc(op + "+");
    bacctracc(op + "-");
    bacctracc(op + "X");
}
