const int MOD = 1e9 + 7;
const int MAXN = 205;
const int DELTA = 40005;

int dp[MAXN][DELTA * 2 + 7];

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    int n, m;
    cin >> n >> m;

    dp[0][DELTA + m] = 1;

    for (int i = 0; i < n; ++i) {
        for (int j = -DELTA; j <= DELTA; ++j) {
            int idx = j + DELTA;
            dp[i + 1][idx + i + 1] += dp[i][idx];
            dp[i + 1][idx - i - 1] += dp[i][idx];
            dp[i + 1][idx] += dp[i][idx];
            dp[i + 1][idx + i + 1] %= MOD;
            dp[i + 1][idx - i - 1] %= MOD;
            dp[i + 1][idx] %= MOD;

        }
    }

    cout << dp[n][DELTA];
    return 0;
}