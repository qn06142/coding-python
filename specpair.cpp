int main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; ++i) {
        cin >> a[i];
    }

    // Step 1: Generate all special numbers up to 2*10^6
    unordered_set<int> special_numbers;
    for (int d = 1; d <= 9; ++d) {
        int num = d;
        while (num <= 2 * 1000000) {
            special_numbers.insert(num);
            num = num * 10 + d;
        }
    }

    // Step 2: Create a frequency hashmap for all elements in array a
    unordered_map<int, int> freq;
    for (int num : a) {
        freq[num]++;
    }

    // Step 3: Count the number of special pairs
    long long count = 0;
    for (int num : a) {
        // Decrease the frequency of current number to avoid using the same element twice
        freq[num]--;
        for (int special : special_numbers) {
            int complement = special - num;
            if (freq.find(complement) != freq.end() && freq[complement] > 0) {
                count += freq[complement];
            }
        }
    }

    cout << count << endl;
    return 0;
}