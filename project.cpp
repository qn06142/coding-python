struct Key {
    int month;
    int workers;
    bool operator==(const Key &other) const {
        return month == other.month && workers == other.workers;
    }
};

namespace std {
    template <>
    struct hash<Key> {
        size_t operator()(const Key &k) const {
            return hash<int>()(k.month) ^ hash<int>()(k.workers);
        }
    };
}

unordered_map<Key, long long> memo;

long long minCost(int n, long long H, long long S, long long D, const vector<int> &a, int month, int workers) {
    if (month == n) {
        return workers * D; 
    }

    Key currentKey = {month, workers};
    if (memo.find(currentKey) != memo.end()) {
        return memo[currentKey];
    }

    long long cost = LLONG_MAX;

    for (int hire = max(0, a[month] - workers); hire <= a[month]; ++hire) {
        int new_workers = workers + hire;
        long long current_cost = hire * H + new_workers * S + minCost(n, H, S, D, a, month + 1, new_workers);
        cost = min(cost, current_cost);
    }

    memo[currentKey] = cost;
    return cost;
}

int main() {
    int n;
    cin >> n;

    long long H, S, D;
    cin >> H >> S >> D;

    vector<int> a(n);
    for (int i = 0; i < n; ++i) {
        cin >> a[i];
    }

    memo.clear();
    long long total_cost = minCost(n, H, S, D, a, 0, 0);

    cout << total_cost << endl;

    return 0;
}