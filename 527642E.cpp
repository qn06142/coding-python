#define int long long
long long sumcont(long long n) {
    return (n * (n + 1)) / 2;
}

long long getsumrange(long long l, long long r) {
    return sumcont(r) - sumcont(l - 1);
}
long long count_b(long long lowerb, long long upperb, long long lowerc, long long upperc, int a) {
    int min_b = max(lowerb, lowerc + a), max_b = min(upperb, upperc - a);
    return (max_b - min_b + 1);
}
bool check(int lower, int upper, int x) {
    return lower <= x and x <= upper;
}

string find(int A, int B, int C, long long k) {
    long long min_a = pow(10, A - 1);
    long long max_a = pow(10, A) - 1;
    long long min_b = pow(10, B - 1);
    long long max_b = pow(10, B) - 1;
    long long min_c = pow(10, C - 1);
    long long max_c = pow(10, C) - 1;
    int l = min_a, r = max_a;
    int a;
    while(l <= r) {
        int mid = (l + r) / 2;
        cerr << mid << ' ';
        int cnt_b = count_b(min_b, max_b, min_c, max_c, mid);
        if(cnt_b <= 0) {
            l = mid + 1;
        }
        int cnt_b_total = getsumrange(cnt_b, max((long long) 0, cnt_b - mid + 1));
        if(cnt_b_total < k) {
            a = mid + 1;
            l = mid + 1;
        } else {
            r = mid - 1;
        }
    }
    for(int b = min_b; b <= max_b; b++) {
        if(min_c <= a + b and a + b <= max_c) {
            return to_string(a) + " + " + to_string(b) + " = " + to_string(a + b);
        }
    }
    #ifdef DEBUG
    cerr << endl;
    #endif
    return "-1";
}

signed main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    int q;
    cin >> q;

    while(q--) {
        int A, B, C;
        long long k;
        cin >> A >> B >> C >> k;
        cout << find(A, B, C, k) << '\n';
    }

    return 0;
}
