// function to generate the k-th smallest equation
string find_kth_smallest_equation(int A, int B, int C, long long k) {
    // define the range for each number
    long long min_a = pow(10, A - 1);
    long long max_a = pow(10, A) - 1;
    long long min_b = pow(10, B - 1);
    long long max_b = pow(10, B) - 1;
    long long min_c = pow(10, C - 1);
    long long max_c = pow(10, C) - 1;

    vector<string> equations;

    // generate all valid equations
    for (long long a = min_a; a <= max_a; ++a) {
        if(a + max_b < min_c) continue;
        for (long long b = min_b; b <= max_b; ++b) {
            long long c = a + b;
            if (c >= min_c && c <= max_c) {
                equations.push_back(to_string(a) + " + " + to_string(b) + " = " + to_string(c));
            }
            if(c > max_c) {
                break;
            }
        }
    }

    // sort equations lexicographically
    sort(equations.begin(), equations.end());

    // return the k-th smallest equation if it exists
    if (k <= equations.size()) {
        return equations[k - 1];
    } else {
        return "-1";
    }
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t;
    cin >> t;

    while (t--) {
        int A, B, C;
        long long k;
        cin >> A >> B >> C >> k;

        cout << find_kth_smallest_equation(A, B, C, k) << "\n";
    }

    return 0;
}
