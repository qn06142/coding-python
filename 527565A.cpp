#define int long long
const int LIM = 40;

struct Node {
    int child[2], cnt;
    Node() {
        child[0] = child[1] = -1;
        cnt = 0;
    }
} node[(int) 1e7 + 6];

int numNode = 0;

void add_num(int root, long long x, long long d) {
    int p = root;
    for (int i = LIM; i >= 0; i--) {
        int bit = (x >> i) & 1;
        if (node[p].child[bit] == -1) node[p].child[bit] = ++numNode;
        p = node[p].child[bit];
        node[p].cnt+=d;
    }
}

long long cnt_x_in_trie(int root, long long x) {
    int p = root;
    for (int i = LIM; i >= 0; i--) {
        int bit = (x >> i) & 1;
        if (node[p].child[bit] == -1) return 0;
        p = node[p].child[bit];
    }
    return node[p].cnt;
}

void del_num(int root, long long x, long long d) {
    d = min(d, cnt_x_in_trie(root, x));
    int p = root;
    for (int i = LIM; i >= 0; i--) {
        int bit = (x >> i) & 1;
        p = node[p].child[bit];
        node[p].cnt-=d;
    }
}

int get_kth(int root, long long k) {
    int p = root;
    long long ans = 0;
    for(int i = LIM; i >= 0; i--) {
        if(p == -1) return p;
        if(node[node[p].child[0]].cnt < k) {
            k -= node[node[p].child[0]].cnt;
            p = node[p].child[1];
            ans^= 1LL << i;
        }
        else p = node[p].child[0];
    }
    if (p == -1) return -1;
    return ans;
}
signed main() {
    ios_base::sync_with_stdio(0);
    cin.tie(0);
    int Q;
    cin>>Q;
    int root = 0;
    numNode = 0;
    while (Q--) {
        int t;
        long long x, d;
        cin>>t;
        if (t == 1) {
            cin>>x>>d;
            add_num(root, x, d);
        }
        else
        if (t == 2) {
            cin>>x>>d;
            del_num(root, x, d);
        }
        else {
            long long k;
            cin>>k;
            cout<<get_kth(root, k)<<"\n";
        }
    }

}