#define int long long
int n, m;
char mat[405][405];
int f(char x, char y, char z) {
    int ans = 0;
    vector<int> heights(m + 1);
    vector<int> pref(m + 1);
    for(int i_ = 1; i_ <= n; i_++) {
        for(int i = 1; i <= m; i++) {
            char tmp = mat[i_][i];
            if(tmp == x or tmp == y or tmp == z) {
                heights[i]++; 
            } else {
                heights[i] = 0;
            }
        }
        #ifndef ONLINE_JUDGE
        for(auto i:heights) {
            cerr << i << ' ';
        }
        cerr << endl;
        #endif
        stack<int> stacc1, stacc2;
        vector<int> l(m + 1);
        for(int i = 1; i <= m; i++) {
            while(!stacc1.empty() and heights[stacc1.top()] >= heights[i]) {
                stacc1.pop();
            }
            l[i] = stacc1.empty() ? 1 : stacc1.top() + 1;
            stacc1.push(i);
        }
        for(int i = 1; i <= m; i++) {
            pref[i] =  ((i - l[i] + 1)) * heights[i] + pref[l[i] - 1];
            ans += pref[i];
        }
    }
    return ans;
}

