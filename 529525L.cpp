const int MOD = 14062008;

int main() {
    int n, k;
    cin >> n >> k;
    
    unordered_set<int> broken;
    for (int i = 0; i < k; ++i) {
        int broken_step;
        cin >> broken_step;
        broken.insert(broken_step);
    }
    
    vector<int> dp(n + 1, 0);
    dp[1] = 1;

    for (int i = 2; i <= n; ++i) {
        if (broken.find(i) != broken.end()) {
            dp[i] = 0;
        } else {
            dp[i] = dp[i-1] % MOD;
            if (i > 2) {
                dp[i] = (dp[i] + dp[i-2]) % MOD;
            }
        }
    }
    
    cout << dp[n] % MOD << endl;
    
    return 0;
}
