int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);
    string S;
    int n;
    cin >> n;
    cin >> S;
    vector<int> S_(n + 1);
    sort(S.begin(), S.end());
    for(int i = 1; i <= S.size(); i++) {
        S_[i] = S[i - 1] - '0';
    }
    //stores the remainder to explore
    queue<int> que;
    //dp[i]: is there a way to reach remainder i mod n
    vector<int> dp((int) 1e6 + 5, {-1});
    // prev[i].first is previous char that made char i with remainder prev[i].second
    vector<pair<int, int>> prev(1e6 + 5, {-1, -1});
    //initial states
    for(int i = 1; i <= S.size(); i++) {
        if(S_[i] == 0) continue;
        int cur = S_[i];
        prev[cur % n] = {cur, -1};
        que.emplace(cur % n);
        dp[cur % n] = 1;
    }
    //cout << "bfs :D" << endl;
    while(!que.empty()) {
        int cur = que.front();
        que.pop();
        for(int i = 1; i <= S.size(); i++) {
            if(dp[(cur * 10 + S_[i]) % n] == -1) {
                dp[(cur * 10 + S_[i]) % n] = 1;
                que.push((cur * 10 + S_[i]) % n);
                prev[(cur * 10 + S_[i]) % n] = {S_[i], cur};
            }
        }
    }
    //cout << "getting dat number" << endl;
    if(dp[0] == -1) {
        cout << 0 << endl;
        return 0;
    }
    string ans = "";
    int cur = 0;
    while(cur >= 0) {
        pair<int, int> yes = prev[cur];
        cur = yes.second;
        ans += yes.first + '0';
        //cout << cur << endl;
    }
    reverse(ans.begin(), ans.end());
    cout << ans;
}
