void prep(vector<long long>& stor) {
    for(long long i = 2; i <= 1e9; i *= 2) {
        stor.push_back(i);
    }
    for(long long i = 3; i <= 1e9; i *= 3) {
        stor.push_back(i);
    }
    for(long long i = 5; i <= 1e9; i *= 5) {
        stor.push_back(i);
    }
    stor.push_back(1);
    sort(stor.begin(), stor.end());
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);
    int N, M;
    cin >> N >> M;

    vector<long long> a(N);
    map<int, int> mp;
    for (int i = 0; i < N; ++i) {
        cin >> a[i];
        mp[a[i]] ++;
    }

    vector<long long> stor;
    prep(stor);
    for(auto i:stor) {
        cerr << i << ' ';
    }
    cerr << endl;
    for (int i = 0; i < M; ++i) {
        long long low, high;
        cin >> low >> high;

        auto start = lower_bound(stor.begin(), stor.end(), low) - stor.begin();
        auto end = upper_bound(stor.begin(), stor.end(), high) - stor.begin();
        int count = 0;
        for(int i = start; i < end; i++) {
            count += mp[stor[i]];
        }
        cout << count << endl;
    }

    return 0;
}