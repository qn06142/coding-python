int n;
int mat[505][505];
const int dx[] = {-1, 1, 0, 0};
const int dy[] = {0, 0, -1, 1};

bool bfs(int maxDiff) {
    bool visited[505][505] = {false};
    queue<pair<int, int>> que;
    que.push({0, 0});
    visited[0][0] = true;
    while (!que.empty()) {
        auto [x, y] = que.front();
        que.pop();
        if (x == n - 1 and y == n - 1) return true;
        for (int i = 0; i < 4; ++i) {
            int nx = x + dx[i], ny = y + dy[i];
            if (nx >= 0 and nx < n and ny >= 0 and ny < n and !visited[nx][ny]) {
                if (abs(mat[x][y] - mat[nx][ny]) <= maxDiff) {
                    que.push({nx, ny});
                    visited[nx][ny] = true;
                }
            }
        }
    }
    return false;
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    cin >> n;
    for (int i = 0; i < n; ++i) {
        for (int j = 0; j < n; ++j) {
            cin >> mat[i][j];
        }
    }

    int left = 0, right = 1e6, answer = right;
    while (left <= right) {
        int mid = left + (right - left) / 2;
        if (bfs(mid)) {
            answer = mid;
            right = mid - 1;
        } else {
            left = mid + 1;
        }
    }

    cout << answer << '\n';
    return 0;
}
