#define int long long
int num_even (int x) {
    string s = to_string(x);
    reverse(s.begin(), s.end());

    for(int i = 0; i < s.size(); i++) {
        x = x * 10 + s[i] - '0';
    }
    return x;
}

int even(int n) {
    int l = 1, r = 1e9;
    int mid, ans = 0;
    while(l <= r) {
        mid = (l + r) / 2;
        int x = num_even(mid);
        if(x <= n) {
            ans = mid;
            l = mid + 1;
        } else {
            r = mid - 1;
        }
    }
    return ans;
}
int num_odd (int x) {
    string s = to_string(x);
    reverse(s.begin(), s.end());

    for(int i = 1; i < s.size(); i++) {
        x = x * 10 + s[i] - '0';
    }
    return x;
}

int odd(int n) {
    int l = 1, r = 1e9;
    int mid, ans = 0;
    while(l <= r) {
        mid = (l + r) / 2;
        int x = num_odd(mid);
        if(x <= n) {
            ans = mid;
            l = mid + 1;
        } else {
            r = mid - 1;
        }
    }
    return ans;
}
int calc(int n) {
    return even(n) + odd(n);
}
